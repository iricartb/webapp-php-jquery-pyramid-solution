   <div class="subtitle">
      3.1 Teoría funeraria de Dormion & Goidin
   </div>
   <div class="paragraph">
      A partir de los supuestos defectos o anomalías técnicas, observadas en determinados lugares de la pirámide, el proyectista Gilles Dormion y el arquitecto Jean-Patrice Goidin, desarrollan una nueva teoría funeraria que sitúa la verdadera cripta de Quéope, oculta en el punto donde se cruzan las medianas de la pirámide; es decir, a nivel del primer techo de la Cámara del Rey y en el lado occidental superior de la Gran Galería. 
   </div>
   <div class="paragraph">
      De acuerdo con esta teoría, la misión de todo el conjunto de cámaras y pasajes conocidos hasta ahora, sería únicamente la de confundir y desorientar a los posibles violadores.
   </div>
   <div class="paragraph">
      La ruta de acceso a esta estancia, desde el exterior, se iniciaría en una puerta escondida tras las enormes placas dintel que cubren el Pasaje Descendente. El nuevo corredor se prolongaría horizontalmente en dirección sur hasta aproximarse al Pasaje Ascendente.
   </div>
   <div class="paragraph">
      Proseguiría, por encima de este, con trayectoria paralela hasta alcanzar la Gran Galería. Un andamiaje colocado a nivel de la regata que recorre el tercer saledizo, permitiría salvar la longitud de la galería y llegar a una zona situada por encima de la Antecámara; desde donde se accedería a la verdadera cámara funeraria <font class="mark_text">*(Fig.6)</font>. 
   </div>
   <div class="paragraph">
      La presencia de esta cámara sería, a criterio de los técnicos franceses, la causante de los asentamientos y las grietas observadas en los techos de la Cámara del Rey.
   </div>
   <div class="paragraph">
      La falta de masa, habría provocado un balanceo de toda la estructura de techos hacia el lado norte; a pesar de haber tenido la precaución de situar el último, formado por vigas a contraste, en posición elevada por medio de los cinco niveles de arquitrabes, a fin de que los empujes transmitidos por aquel, no incidieran sobre el espacio ocupado por la cámara sino en la sólida mampostería del núcleo.
   </div>
   <div class="paragraph">
      Una vez depositado el cuerpo momificado de Quéope en su sarcófago, los sacerdotes procederían al sellado definitivo de la cripta, haciendo que un gran rastrillo de piedra se desprendiera y bloqueara la entrada.
   </div>
   <div class="paragraph">
      El mecanismo de accionado de este sistema de cierre, estaría situado en el habitáculo conocido como Antecámara, oculto tras la pared del lado oeste y disimulado por las losas móviles del falso dintel, del que a su vez formaban parte. 
   </div>
   <div class="paragraph">
      Dos libros propios: <i>"Kheops: Nouvelle enquête"</i>, 1986 y <i>"Les nouveaux Mystères de la Grande Pyramide"</i>, 1987, reúnen todas sus investigaciones e hipótesis. 
   </div>
