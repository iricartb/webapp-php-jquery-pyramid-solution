<div id="section">
   <div class="title">
      3. Teorías funerarias
   </div>
   <div class="paragraph">
      La mayoría de investigadores y arqueólogos, Petrie, Borchardt, Edwards, Grinsell, Vandier, Goyon, Lauer, Lehner, están de acuerdo en que la finalidad de las pirámides era servir de sepulcro inviolable al faraón reinante. 
   </div>
   <div class="paragraph">
      Asignar una función exclusivamente funeraria a todo el conjunto de cámaras interiores de la pirámide de Quéope exige, para justificar la presencia de tres cámaras funerarias, a diferentes niveles, dos cambios de plan sobre el proyecto original <font class="mark_text">*(Fig.1)</font>.  
   </div>
   <div class="paragraph">
      Las fases de construcción serían las siguientes:
   </div>
   <div class="paragraph_p40">   
      - Al tiempo que se edificaba la base de la pirámide, se excavó el Pasaje Descendente y la Cámara Subterránea; con el propósito de que fuera esta la depositaria de los restos mortales del rey. Cuando la fabrica había alcanzado el nivel que corta el Pasaje Ascendente en su mitad, zona a partir de la cual los sillares que forman este pasaje adoptan diferente posición, se decidió construir una nueva cámara dentro del núcleo de mampostería: la Cámara de la Reina. Se interrumpieron los trabajos subterráneos y dejaron la cámara en el estado actual. Posteriormente, cuando todavía no habían terminado el suelo de esta nueva estancia y los respiraderos estaban en su fase inicial, decidieron que la cripta ocupara un nivel más alto dentro de la obra: la Cámara del Rey. Es por ello que construyeron la Gran Galería y la Antecámara.
   </div>
   <div class="paragraph_p40">   
      - A la muerte del faraón, de acuerdo con la teoría funeraria, la comitiva fúnebre hubo de penetrar en la pirámide por la entrada de la cara norte, bajar por el estrecho Pasaje Descendente hasta alcanzar la confluencia con el Pasaje Ascendente. Subir por este conducto hasta alcanzar la Gran Galería. Aquí pudieron erguirse pero la pendiente seguía siendo la misma. Después de sortear los bloques obstructores, dispuestos para el sellado de la pirámide, hubieron de salvar la altura del Gran Peldaño y previa genuflexión, accedieron a la Antecámara. Una vez más tuvieron que inclinarse antes de penetrar en la estancia destinada a ser el sepulcro del monarca más poderoso de su tiempo: la Cámara del Rey, en cuyo sobrio sarcófago depositaron el cadáver momificado.
   </div>
   <div class="paragraph_p40">   
      - Finalizada la ceremonia, los sacerdotes, sellaron la cámara desprendiendo los tres rastrillos de piedra encajados en las ranuras laterales de la Antecámara <font class="mark_text">*(Fig.2)</font>. 
   </div>
   <div class="paragraph_p40">   
      - Acto seguido hicieron que los tres grandes bloques de granito se deslizaran libremente sobre el piso de la Gran Galería; cruzaran el puente sobre la zona de confluencia con el corredor llano que conduce a la Cámara de la Reina, y penetraran velozmente en el Pasaje Ascendente hasta topar contra la estrechez del extremo inferior, hecha a propósito para retener la cuña obstructora y bloquear el paso a las cámaras superiores.
   </div>
   <div class="paragraph_p40_last">   
      - Por último, los sacerdotes, bajarían por el tortuoso Pozo hasta dar con el Pasaje Descendente, que les conduciría al exterior. 
   </div>
