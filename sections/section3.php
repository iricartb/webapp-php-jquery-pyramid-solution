   <div class="subtitle">
      3.4 La nueva hipótesis de Gilles Dormion 
   </div>
   <div class="paragraph">
      Su última propuesta es radicalmente diferente de lo que hasta ahora había defendiendo y que ha sido expuesto anteriormente en este mismo capítulo.
   </div>
   <div class="paragraph">
      Para proceder a su análisis creo conveniente transcribir en primer lugar la síntesis de la teoría que proporciona al final de su libro: La chambre de Chéops; añadiendo y modificando algunos términos del texto para hacerlo más comprensivo:
   </div>
   <div class="paragraph">
      <i>«El primer proyecto comprende la excavación subterránea y profunda de una cámara funeraria. Su entrada se halla en el lado norte de la pirámide. Este proyecto permanece aún inconcluso.
   </div>
   <div class="paragraph">
      Mientras que el núcleo escalonado de la pirámide se eleva, otro apartamento funerario es preparado en la mampostería de la pirámide: esto constituye un segundo proyecto, totalmente independiente del primero y con la entrada localizada en el lado este <font class="mark_text">*(Fig.8)</font>. Más complejo que el anterior, prevé la construcción de unos almacenes con entrada propia a través del lado norte. La única conexión entre ambos sistemas es un pozo practicado en el nicho de la actual Cámara de la Reina.
   </div>
   <div class="paragraph">
      El funcionamiento previsto para estos apartamentos funerarios es el siguiente: El acceso se hace a través de una entrada en el lado oriental; según un esquema que resulta imposible de concretar por el momento. A continuación se encuentra el pasaje de la cámara de los cerramientos o rastrillos (delante del nicho); el pequeño ensanchamiento y, en frente, el pasaje de entrada a la cámara, previo rastrillo. Por encima de todo ello se halla el pasaje que conduce a los almacenes (en el corredor de la actual Cámara de la Reina); independientes y utilizables a través de la entrada emplazada en el lado norte.
   </div>
   <div class="paragraph">
      Después de los funerales, la clausura se efectúa saliendo por el lado norte. Se procede al desprendimiento de los tres rastrillos de entrada, y el conducto de servicio es bloqueado. El rastrillo del lado oeste (este?) de la cámara funeraria es desprendido, después la trampilla cerrada y el pozo del nicho colmatado. La salida se efectúa por los almacenes. Resta únicamente desprender los tres (supuestos) cerramientos o rastrillos del pasaje ascendente.
   </div>
   <div class="paragraph">
      Por consiguiente, antes de la finalización de este sistema, un tercer proyecto es emprendido por una razón tan importante como simple: emplazar la cámara un nivel más alto, a fin de soportar la menor carga posible. Era necesario pues prolongar la estructura existente. Resulta imposible conectar con la distribución primaria oriental, dado que la prolongación repercutiría en el recubrimiento de las cámaras: el de la cámara funeraria del segundo proyecto, inconcluso, y el de la Cámara de la Reina, todavía en fase de construcción. Esto conducirá a la futura cámara funeraria, emplazada mucho más hacia el oeste (sur ?). La única solución posible consiste en prolongar el acceso norte que conduce a los almacenes; este <i>"injerto"</i>, es causa de todas las adaptaciones (constructivas) que hemos analizado a lo largo del libro.
   </div>
   <div class="paragraph">
      La configuración definitiva de este tercer proyecto es acabado mientras concluye la construcción del núcleo de la pirámide. La Cámara del Rey y su elevada estructura (de techos) son recubiertas, para soportar la carga superior. La cámara funeraria definitiva es pues operacional y segura.
   </div>
   <div class="paragraph">
      En el momento en que la segunda fase constructiva empieza, durante el transcurso de la cual la mampostería complementaria es emplazada para dar forma definitiva a la pirámide, la cámara funeraria oficial es la correspondiente a la del tercer proyecto; el segundo resulta ya obsoleto. La entrada oriental, asociada a este último, es inútil a partir de este momento, y será recubierta con mampostería complementaria. Esta recubre también los conductos de <i>"ventilación"</i> de la Cámara de la Reina, ahora totalmente innecesarios. En cambio, los de la Cámara del Rey, son prolongados y acabados.
   </div>
   <div class="paragraph">
      Progresivamente, en sentido ascendente, la sillería se concentra desde el perímetro hacia el centro de la pirámide. En la proximidad de la cima, directamente por encima de la Cámara del Rey, es cuando este exceso de mampostería se acumula.
   </div>
   <div class="paragraph">
      Es en este momento cuando se produce el desastre. En poco tiempo, la carga inicial soportada por la estructura de la cámara, se multiplica por dos. Su sistema de recubrimiento es incapaz de soportarlo y se produce el accidente. Después de la consolidación y la verificación de los daños, se asume la gravedad de la situación: ningún remedio puede garantizar que la superestructura de la cámara no pueda hundirse en cualquier momento. ¡La cámara funeraria del tercer proyecto está fuera de servicio! 
   </div>
   <div class="paragraph">
      Los antiguos egipcios no disponen en esta situación más que de una alternativa: acabar la estancia subterránea, o reactivar la del segundo proyecto. Esta última es en principio finalizada y dotada de un sarcófago. Esta segunda opción es lógicamente la elegida, y la que presenta evidentes y abundantes trazas de intervención. El único inconveniente es que el acceso previsto por el lado este, fue suprimido y se encuentra completamente cubierto por la mampostería complementaria. Subsiste no obstante un pequeño pasaje por el pozo del nicho, (de la actual Cámara de la Reina) aunque inadecuado para el cortejo fúnebre. Se hace indispensable practicar un descenso más apropiado. Las losas de la Cámara de la Reina son desplazadas. La construcción de una escalera es improvisada hacia el pie del nicho. Finalmente el pavimento es reposicionado alrededor de este nuevo elemento arquitectónico.
   </div>
   <div class="paragraph">
      Después de los funerales, desde la Cámara de la Reina, los rastrillos de cierre son despendidos y los accesos a los dispositivos de maniobra colmatados, ocultados y bloqueados. El descansillo norte, en el pasaje horizontal, (en el inicio de la Gran Galería) es rellenado con pequeños sillares y recubierto con un pavimento; de tal forma que la entrada al pasaje resulta imperceptible. Los tres bloques de granito obstructores son introducidos en el pasaje ascendente por obreros que abandonan la pirámide mediante el pozo de servicio. Este último permanece accesible y abierto, mientras que una piedra móvil (según Estrabón) es adaptada a la entrada de modo que permita inspecciones periódicas; exactamente como se hizo en la pirámide Romboidal de Dahshur. La pirámide será profanada en dos ocasiones: primero a través de su verdadera entrada, durante el Primer Periodo Intermedio, y posteriormente mediante la brecha excavada en el siglo IX de nuestra Era por el califa Al-Mamún.»</i> <font class="mark_text">[DORMION, 2004, pp.270-273]</font>.
   </div>
   <div class="paragraph">
      Es decir que durante la construcción de la pirámide se modificó radicalmente el proyecto original dos veces tal como dijo Borchardt. No obstante, hay importantes diferencias en lo que respecta a la segunda fase entre el arqueólogo alemán y el arquitecto francés:
   </div>
   <div class="paragraph">
      Cuando los antiguos egipcios decidieron abandonar la excavación de la cámara subterránea, no solo construyeron la que hoy conocemos como la Cámara de la Reina con sus respiraderos, el pasaje ascendente y el corredor horizontal, sino también unos supuestos almacenes en la parte occidental de este último y también la supuesta cámara funeraria oculta. Cuatro cierres de guillotina protegerían la cripta con los restos de Quéope; los tres primeros se accionaron desde sendos taladros verticales procedentes de un conducto que partía del nicho. El cuarto rastrillo se maniobraría desde el interior de la cámara, a través de un agujero que perforaría el actual pavimento. La decisión de abandonar esta compleja estructura y levantar una nueva cripta a un nivel más alto, para que la presión resultante del peso de la sillería sobre ella fuera menor, comportó la construcción de la Gran Galería como prolongación del pasaje ascendente. Las fisuras que posteriormente aparecieron sobre los arquitrabes de granito que forman los cinco techos determinó el regreso al segundo proyecto, el entibado de la Gran Galería y la modificación del sistema de cierre de la pirámide; sustituyendo los supuestos cierres de tipo rastrillo previstos en el pasaje ascendente, lo que se conoce como <i>"bloques cinturón"</i>, por el deslizamiento de bloques obstructores de granito a través de un pavimento que daría continuidad al piso de la galería sobre la zona abierta que constituye la confluencia de los tres pasajes; aunque previamente debería rellenarse en su totalidad con pequeños sillares.
   </div>
   <div class="paragraph">
      Durante el sepelio del rey, hubo que improvisar la construcción de una escalera que comunicara las dos cámaras, puesto que el acceso natural procedente de la cara este había sido ya anulado. Esto exigió levantar y remover los bloques que forman el actual piso de la Cámara de la Reina.
   </div>
   <div class="paragraph">
      La posición de la supuesta cámara vendría determinada por la interpretación de los sondeos efectuados en noviembre de 2000, por la sociedad francesa SAFEGE, mediante un radar geológico prestado por la sociedad ABEM France; pero también para explicar porqué la pared sur de la Cámara del Rey descendió en toda su longitud y de forma uniforme y no así la pared norte de la misma. Esto lleva a Dormion a imaginar un techo que recuerda al de la Cámara del Rey, aunque provisto de una sola <i>"cámara de descarga"</i>. Es decir que la pared norte de la Cámara del Rey no tuvo asentamientos porque en su parte inferior se hallan, en la misma vertical, los techos a jabalcón de ambas cámaras: la de la Reina y la supuestamente verdadera.
