   <div class="subsubtitle">
      Objeciones
   </div>

   <div class="paragraph_p40">   
      - Las precisas comprobaciones efectuadas por Jean Kerisel <font class="mark_text">[KERISEL,1996, pp.253-256]</font> confirman el descenso de la pared sur de la Cámara del Rey y por tanto invalidan la hipótesis del balanceo de la cámara hacia el norte propuesta por Gilles Dormion y Jean-Patrice Goidin. Si a ello añadimos que los sondeos por medio de ondas electromagnéticas efectuadas por la Universidad de Waseda (Japón) <font class="mark_text">[YOSHIMURA, 1987-1988, II, p.48]</font> no detectaron ningún vacío o defecto de masa sobre la Antecámara, la ya ilógica teoría de la cámara funeraria oculta en esta zona, se queda sin argumentos que la puedan avalar. 
   </div>
   <div class="paragraph_p40">   
      - La teoría propuesta por los arquitectos franceses, no justifica la necesidad de construir una estructura de techos tan compleja y costosa. Existen soluciones más simples, en este caso, para evitar la transmisión de los esfuerzos. Si la verdadera cámara funeraria fuera la que intuyen, nada impediría que la actual Cámara del Rey, provista de un techo como el de la Cámara de la Reina, ocupara una posición más alejada, baja o alta respecto de su homóloga. Otra solución sería que, sin modificar la disposición actual de las cámaras, la supuesta cámara verdadera ocupara otro nivel sobre el eje de simetría. 
   </div>
   <div class="paragraph_p40">   
      - Nada podía garantizar a los arquitectos egipcios que en caso de violación de la pirámide, antes se localizarían las cámaras y los pasajes conocidos, que los que son objeto de la teoría que estamos analizando.
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      3.2 Teoría funeraria de Jean Kerisel 
   </div>
   <div class="paragraph">
      Director general honorario del Ministerio de la Reconstrucción, fue presidente de la sociedad de ingenieros y científicos de Francia. Es una de las mayores autoridades científicas de Francia. La búsqueda de recursos para salvar el gran templo de Abu-Simbel y la estancia en Egipto como consejero durante la construcción del metro de El Cairo, despertaron su interés por la egiptología y concretamente por La Gran Pirámide. 
   </div>
   <div class="paragraph">
      Aunque también defiende el carácter funerario de las cámaras, por su singularidad esta teoría, requiere no obstante un análisis particularizado: 
   </div>
   <div class="paragraph">
      De acuerdo con el testimonio de Herodoto, Quéope, orgulloso y obstinado, desoyó el clamor del pueblo y el consejo de los sacerdotes por una obra tan costosa y fatigante. Se empecinó hasta el extremo de llegar a dirigir personalmente la construcción de la pirámide a partir del nivel de la Gran Galería. Esto explicaría las numerosas anomalías técnicas que se observan en la Cámara del Rey. La insólita y complicada estructura de techos, obedecería a un error de concepción. Las vigas se fisuraban durante la construcción de forma que, Quéope, lo interpretó como una señal de los dioses para castigar su orgullo. Es por ello que decide no enterrarse en la cripta o Cámara del Rey. Avala el testimonio de Herodoto cuando dice que Quéope fue enterrado en una tumba subterránea rodeada de agua que llegaba del Nilo a través de un canal excavado a tal fin. Kerisel hace la observación de que únicamente Quéope y su padre Esnofru habían osado alojar la cámara funeraria en el núcleo de la pirámide. Ambas experiencias habían resultado funestas dada la aparición de graves fisuras. En adelante las pirámides volverían al primitivo diseño de la cripta subterránea <font class="mark_text">[KERISEL, 1996, p. 262]</font>. 
   </div>
   
   <div class="separator"></div>
   
   <div class="subsubtitle">
      Objeciones
   </div>
   
   <div class="paragraph_p40">   
      - Resulta inverosímil que siendo Quéope uno de los faraones más poderosos de la historia de Egipto y gozando del carácter divino de los reyes del Imperio Antiguo, pudiera perder la sumisión e incluso la colaboración de sus consejeros y sacerdotes hasta el extremo de tener que proyectar y dirigir las obras de su cámara funeraria. 
   </div>
   <div class="paragraph_p40">   
      - Quéope no hubiera atribuido las fisuras de las jácenas a una maldición de los dioses por el hecho de haber reincidido en la idea de su padre de situar la cámara a mayor nivel puesto que, previamente y con el mismo fin (según la teoría funeraria) había construido la Cámara de la Reina sin constatar la oposición Divina. 
   </div>
   <div class="paragraph_p40">   
      - La pirámide Roja de Dahshur dispone de una cámara, muy por encima del nivel del suelo, en la cual no se observan daños capaces de impedir el eterno descanso de Esnofru.
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      3.3 La hipótesis de Jacques Bardot & Francine Darmon 
   </div>
   <div class="paragraph">
