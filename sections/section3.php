   <div class="subsubsubtitle">
      Sobre su violación y saqueo 
   </div>
   <div class="paragraph_p40">   
      - Si las cámaras eran funerarias, tuvieron que ser violadas antes de la dominación árabe; dado que los hombres de Al-Mamún, que penetraron en la pirámide excavando en los sillares, encontraron las cámaras totalmente vacías y el sarcófago sin tapa.
   </div> 
   <div class="paragraph_p40">   
      - Si el relato de los historiadores árabes es cierto, la única ruta posible para los saqueadores era el conducto del Pozo; pero este se encontró obstruido y es imposible, tal como ya se ha dicho, hacerlo desde la parte inferior. Resulta además innecesaria su oclusión tratándose de un saqueo. 
   </div> 
   <div class="paragraph_p40">   
      - El laborioso y seguro sistema de cierre por medio de la cuña de granito sería inútil si existiera la posibilidad de acceder fácilmente a las cámaras a través del pozo. 
   </div> 
   <div class="paragraph_p40_last">   
      - Por otra parte es increíble que ladrones pudieran excavar 60 m de conducto en sentido ascendente y remotamente improbable que encontraran la Gran Galería.
   </div>

