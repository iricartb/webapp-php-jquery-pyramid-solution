   <div class="subsubtitle">
      Objeciones
   </div>
   
   <div class="subsubsubtitle">
      Respecto a la construcción 
   </div>
   <div class="paragraph_p40">   
      - Dos cambios de plan sobre el proyecto original implica improvisación en una obra destinada a mantener ocupado a todo el pueblo egipcio durante tres décadas. Resulta difícil de creer que de tal improvisación pudiera resultar una obra tan perfecta. 
   </div>
   <div class="paragraph_p40">   
      - La existencia de unas excavaciones subterráneas a 87,5 m al este de la Gran Pirámide, en las cuales se observa el mismo diseño de pasajes que en la pirámide, demuestra que no hubo tal improvisación <font class="mark_text">*(Fig.3)</font>.
   </div>
   <div class="paragraph_p40">   
      - Tal como indican los arquitectos Maragioglio y Rinaldi, en contra de la opinión de Borchardt, la presencia de las grandes piedras a modo de cinturones o amarres, a la distancia de 10 codos en el Pasaje Ascendente y el mejor acabado de los bloques en esta zona, respecto a los sillares que forman el núcleo de mampostería, demuestran que la pirámide se concibió así desde un principio:
   </div>
   <div class="paragraph_p80">   
      <i>"De todo este conjunto de elementos se puede afirmar que el razonamiento de Borchardt, en cuanto a los tres períodos de construcción de la pirámide, no es aceptable. A nuestro modo de ver, todas las cámaras construidas en la pirámide se corresponden con un único proyecto; completamente y minuciosamente estudiado hasta el último detalle"</i> <font class="mark_text">[MARAGIOGLIO & RINALDI, 1965, IV, p.153, Obs.42]</font>.
   </div>
   <div class="paragraph_p40">   
      - El diseño de los pasajes y cámaras no se corresponde, desde un punto de vista funcional y lógico, con el que debería tener en base a una finalidad funeraria.
   </div>
   <div class="paragraph_p40">   
      - La Cámara de la Reina no dispone de sistema de sellado.
   </div>
   <div class="paragraph_p40">   
      - Resulta inexplicable e insólita la presencia de respiraderos en la Cámara de la Reina y en la Cámara del Rey; aun más tratándose de una tumba. Ninguna otra pirámide dispone de estos conductos. 
   </div>
   <div class="paragraph_p40">   
      - El descubrimiento en 1993 de la verdadera longitud del respiradero sur de la Cámara de la Reina, invalida el principal argumento a favor del segundo cambio de plan. En efecto, con 60 m de longitud sobrepasa el nivel de la Cámara del Rey. Sería absurdo, una vez abandonada la construcción de la Cámara de la Reina, seguir prolongando este costoso conducto.
   </div>
   <div class="paragraph_p40_last">   
      - El sarcófago no dispone de tapa y no se han hallado restos de la misma. Carece de inscripción alguna y no recibió el pulido final; es más, se pueden observar en él señales producidas por el desvió involuntario de la sierra durante el proceso de corte. 
   </div>
   <div class="gallery_border">
      <a class="fancybox-thumb" rel="gallery2" href="../../img/section3/fig3.jpg" title="Figura 3. El triple pasaje subterráneo">
         <img src="../../img/section3/fig3.jpg" alt="Figura3" />
      </a>
   </div>
   
   <div class="separator"></div>
   
   <div class="subsubsubtitle">
      Respecto al sepelio 
   </div>
   <div class="paragraph_p40">   
      - El acceso al Pasaje Ascendente a partir del Descendente, sería muy dificultoso para la comitiva fúnebre con los restos mortales del faraón.
   </div>
   <div class="paragraph_p40">   
      - El Pozo no podía constituir la ruta de escape dado que su tramo final, por debajo de la Gruta, se encontró embozado; según testimonio de <br/>J. Greaves, N. Davison y el capitán Caviglia.
   </div>
   <div class="paragraph_p40_last">   
      - Es imposible taponar el Pozo hasta este nivel procediendo desde la parte inferior del mismo.
   </div>
   
   <div class="separator"></div>
   
   <div class="subsubsubtitle">
      Respecto al sellado de la Cámara del Rey 
   </div>
   <div class="paragraph_p40">   
      - No se han encontrado restos de los hipotéticos rastrillos de piedra.
   </div>
   <div class="paragraph_p40">   
      - El piso de la Antecámara no dispone de los necesarios rebajes para que encajen en ellos las piedras de cierre; como es el caso de la pirámide de Quefrén. Es decir que el diseño no se corresponde con este sistema de cierre; es más, los rebajes se hallan en las paredes laterales, como prolongación de las canaladuras.
   </div>
   <div class="paragraph_p40">   
      - El falso dintel de la Antecámara, compuesto por una piedra fija y otra superior desplazable tampoco se ajusta a la función atribuida que es la de sujetar las cuerdas que impedían el descenso de los rastrillos.
   </div>
   <div class="paragraph_p40">   
      - Únicamente la pared este de la Antecámara dispone de rebajes semicirculares para albergar los rodillos de madera que deberían facilita el descenso de los rastrillos.
   </div>
   <div class="paragraph_p40">   
      - Este sistema de cierre resulta ineficaz. La ausencia de rebajes en el piso y la escasa altura de los rastrillos permitiría su desplazamiento con ayuda de palancas.
   </div>
   <div class="paragraph_p40_last">   
      - Las piedras serían accesibles por la parte superior, lo que haría muy fácil su esculpido. Maragioglio y Rinaldi, intentan salvar este sistema de cierre rellenando la zona superior de los rastrillos por medio de sillares; no obstante, nada impediría que estos pudieran ser desalojados posteriormente.
   </div>
   
   <div class="separator"></div>
   
   <div class="subsubsubtitle">
      Respecto al sellado de la pirámide 
   </div>
   <div class="paragraph_p40_m13">   
      - La ubicación provisional de los bloques obstructores antes de cerrar la pirámide, constituye uno de los escollos más importantes para las teorías que atribuyen una finalidad funeraria a las cámaras interiores. Los bloques no podían estar en el Pasaje Ascendente dado que obstruirían el paso y acceso a las cámaras. Tampoco en la confluencia de los tres pasajes, puesto que ocuparían toda esta zona y, por consiguiente, impedirían la entrada por el Pasaje Ascendente. Petrie ha demostrado la imposibilidad de que fueran introducidos en el corredor de la Cámara de la Reina, debido a que la altura del pasaje es cerca de 3 cm. inferior a la altura de los bloques. Únicamente la Gran Galería podía albergar la cuña obstructora; pero, suponiendo que así fuera, surge el mismo dilema al tener que decidir que parte de la misma era la depositaria.
   </div>
   <div class="paragraph_p40_m13">   
      Petrie y Goyon proponen que estarían emplazados sobre el piso de la galería, encajados entre las ménsulas.
   </div>
   <div class="paragraph_p40_m13">   
      No obstante, semejante disposición, constituiría un serio e irreverente obstáculo para la comitiva fúnebre <font class="mark_text">*(Fig.4)</font>. 
   </div>
   <div class="paragraph_p40_m13">   
      Borchardt opina que los bloques obstructores, en espera de su utilización, fueron colocados sobre una plataforma de madera situada a media altura de la galería; concretamente a nivel de la entalladura que recorre ambas paredes por encima del tercer saledizo y sustentada por postes encajados en los huecos que taladran las ménsulas.
   </div>
   <div class="paragraph_p40_m13">   
      Esto justificaría la desconcertante y, en apariencia, excesiva altura de la Gran Galería respecto del resto de los pasajes. 
   </div>
   <div class="paragraph_p40_m13">   
      El peligro que representaría transitar con el cuerpo del faraón por debajo de monolitos de casi 6.000 kg, suspendidos sobre una frágil estructura de madera, parece ilógico. A esto hay que añadir la dificultad que entrañaría apear los bloques sobre el piso en pendiente de la galería, desde esta altura, disponiendo de tan poco espacio y todo ello sin causar daños irreparables <font class="mark_text">*(Fig.5)</font>.
   </div>
   <div class="paragraph_p40">   
      - La finalidad funeraria no da respuesta a la función y diseño de los huecos sobre las ménsulas. Si la misión de estas cavidades era la de retener o sostener la cuña obstructora, no se comprende que necesidad hay de que dichos huecos se hallen presentes a lo largo de toda la galería. Tampoco se explica porqué presentan dos clases de longitud en disposición alterna. 
   </div> 
   <div class="paragraph_p40">   
      - El desprendimiento libre e incontrolado de los bloques obstructores hubiera ocasionado la rotura de la estrechez destinada a retener el primer bloque.
   </div>
   <div class="paragraph_p40_last">   
      - El sistema de control propuesto por Goyon, (un hombre provisto de una palanca y situado por delante del primer bloque obstructor) resulta inverosímil.
   </div>
