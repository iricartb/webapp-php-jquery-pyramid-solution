   <div class="subsubtitle">
      Objeciones
   </div>
   <div class="paragraph_p40_m13">   
      - El arquitecto francés Gilles Dormion, disiente de la hipótesis de los arqueólogos franceses y, en su libro: <i>"La chambre de Chéops"</i>, pp.119-122, afirma que en realidad todas las juntas del pasaje son verdaderas. Hace observar que ningún sillar de la pared podría ser desalojado sin que se produjera la inmediata ruptura y desprendimiento de uno o dos bloques que forman el techo en esta parte. Es decir que ningún bloque del techo en todo el corredor horizontal podría ser a su vez dintel de la supuesta puerta. 
   </div>
   <div class="paragraph_p40_m13">   
      Quiero señalar que, según los planos confeccionados por el propio Dormion, uno de los sillares de la pared constituiría una excepción a lo dicho anteriormente; pudiendo ser desalojado sin peligro aparente. Se trata del bloque más cercano a la cámara, de los tres que él mismo perforó en 1986; mide únicamente 89,3 cm.
   </div>
   <div class="paragraph_p40">   
      - La utilización de filtros de primarios para analizar las juntas de los bloques y la textura de la piedra, tal como hacen Bardot & Francine, no tiene porque suministrar imágenes más fidedignas que las obtenidas con luz completa; al contrario: dos tercios de la gama visible de la luz son despreciados en cada toma; es decir que si empleamos un filtro verde, los objetos o pigmentos azules y rojos se verán más o menos oscuros, confundiéndose con los pigmentos negros o grises. Si lo hacemos con un filtro rojo, los verdes y los azules se confundirán entre sí y con los negros o grises, y lo mismo ocurrirá con el azul. Si trabajamos con los primarios sustractivos: cian, amarillo y magenta, utilizamos dos tercios de la gama visible y despreciamos un tercio; así, un filtro cian deja pasar los verdes y los azules e impide el paso de los rojos. El amarillo deja pasar los rojos y los verdes y absorbe a los azules. Finalmente, un filtro magenta deja pasar a los rojos y azules, absorbiendo a los verdes. Lo ideal sería poder añadir al espectro visible las radiaciones infrarrojas y ultravioletas u otras que no son perceptibles. Esto sí proporcionaría información oculta a nuestro sistema de visión.
   </div>
   <div class="paragraph_p40">   
      - El hecho de que algunas juntas dividan texturas o protuberancias de la piedra e incluso fósiles incrustados, no constituye una prueba suficiente de que nos hallamos ante una junta falsa. Es posible que sea así, pero no necesariamente. Podría tratarse de grandes piedras cortadas y separadas en bloques para su transporte y reunidos nuevamente de la misma forma al construir las paredes del corredor. Esto proporcionaría muros tanto o más sólidos que si se desplazaran las juntas para que estas no coincidieran. El contacto entre bloques sería más íntimo sin proceder al desplazamiento de sus juntas; sobre todo si estas forman planos irregulares en su interior.
   </div>
   <div class="paragraph_p40">   
      - ¿Por qué deberían suponer los antiguos egipcios que intuiríamos que la Gran Galería habría sido destinada a estacionar bloques? Y, aunque así fuera, ¿de que serviría disimular su número? No creo que esto les preocupara lo más mínimo.
   </div>
   <div class="paragraph_p40">   
      - Sería prácticamente imposible maniobrar todos estos grandes bloques sobre el piso en pendiente de la galería, en un espacio tan reducido y simplemente con palancas, tal como asegura el propio autor.
   </div>
   <div class="paragraph_p40">   
      - Suponer la existencia de una estructura de cámaras y pasajes oculta tan compleja, basándose únicamente en la apariencia de ciertas juntas y en los cuestionables resultados de algunos sondeos, es poco riguroso. Sus conjeturas carecen de base suficiente y por consiguiente no convencen. Curiosamente J. Bardot & F. Darmon conceden mayor importancia y protagonismo a las supuestas cámaras ocultas que a las conocidas; olvidándose de que estas lo fueron también hasta que los árabes las descubrieron.
   </div>
   
