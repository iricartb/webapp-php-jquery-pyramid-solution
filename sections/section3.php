   <div class="subsubtitle">
      Objeciones
   </div>
   <div class="paragraph_p40_m13">   
      - Es lógica una cierta desconfianza o incredulidad ante la exposición de una hipótesis que se contrapone a casi todo lo que el propio autor de la misma defendía hace pocos años; a saber: 
   </div>
   <div class="paragraph_p80">   
      - Que la verdadera cámara funeraria se hallaba oculta junto a la Cámara del Rey; al norte de la misma y sobe la Antecámara.
   </div>
   <div class="paragraph_p80">   
      - Que el dispositivo de sellado de esta cripta radicaba en el falso dintel de la Antecámara.
   </div>
   <div class="paragraph_p80">   
      - Que las grietas en los arquitrabes de granito de los cinco techos planos obedecía a un desplazamiento hacia el norte experimentado por toda la cámara y sus techos a causa de la presencia de la cripta oculta en esta parte.
