<div id="section">
   <div class="title">
      4. Nueva teoría constructiva
   </div>
   <div class="paragraph">
      La construcción hubo de realizarse en dos fases correlativas pero totalmente diferenciadas:
   </div>
   <div class="paragraph">
      En primer lugar se utilizó el recurso de la rampa o plano inclinado hasta alcanzar la fabrica el nivel correspondiente al primer techo de la Cámara del Rey <font class="mark_text">(Apartado, 4.1)</font>. 
   </div>
   <div class="paragraph">
      A continuación, la obra proseguía mediante la intervención de un avanzado mecanismo, situado en las cámaras interiores de la propia pirámide (del que a su vez formaban parte) y cuya acción se transmitía al aparato elevador de sillares emplazado sobre gradas formadas por el propio revestimiento de la fachada <font class="mark_text">(Apartado, 4.2)</font>. 
   </div>

   <div class="separator"></div>
   
   <div class="subtitle">
      4.1 La rampa de construcción
   </div>
   <div class="paragraph">
      La rampa destinada al acarreo y ascenso de los sillares, estaría emplazada sobre la fachada sur de la pirámide. Su trayectoria se desviaría unos 10º en sentido suroeste y su eje central debería forzosamente coincidir (en su fase final) con la prolongación de la salida del respiradero que parte de la Cámara del Rey. 
   </div>
   <div class="paragraph">
      La parte superior de la hilada 60, dista de la base de la pirámide 49,774 m; exactamente 95 Codos de 0,5239 m. Este era el nivel máximo o altura de la rampa por medio de la cual se colocó, además: la hilada 61 completa y partes progresivamente menores de las hiladas 62-71, siguiendo la prolongación del plano inclinado sobre la terraza en construcción <font class="mark_text">*(Fig.1)</font>. El terraplén, con una pendiente de una parte por cada 18,53 tendría, en su fase final, una longitud semejante a la que Herodoto asigna a la espléndida calzada que costó diez años de trabajo a todo el pueblo egipcio: 5 estadios, y se apartaría 883,53 m del pie de la pirámide. 
   </div>
   <div class="paragraph">
      Esto equivale a un desnivel del 5,4 % (inferior incluso al recomendado por Georges Goyon: 5,6%), inclinación muy adecuada para arrastrar las grandes piedras sin sobrepasar las más lejanas canteras de caliza fósil de la propia meseta, situadas al sureste de la pirámide de Micerino. 
   </div>
   <div class="paragraph">
      Su punto más alejado estaría situado cerca de los llamados Talleres de Quefrén; lugar accesible a través del Valle Principal que separa la Formación de Mokattam de la Formación de Maadi y que constituía la ruta de suministros externos como la piedra caliza procedente de las canteras de Tura y el granito del lejano Asuán, que llegarían en embarcaciones a través del Gran Canal de Menfis (Bahr el-Libeini); antigua ruta fluvial derivada del Nilo, y desembarcados en un puerto común a las tres pirámides, situado en la depresión que se halla al sur de la Gran Esfinge y al este del templo del valle de Micerino <br/><font class="mark_text">[GOYON, 1990, pp.131-137] *(Fig.2)</font>. 
   </div>
   <div class="paragraph">
      A través de esta rampa y su prolongación, subieron también las inmensas piedras de granito que forman los cinco techos planos sobre la Cámara del Rey y las de piedra caliza que, en disposición de gablete, coronan este conjunto arquitrabado. A partir del segundo techo, las vigas eran depositadas en la amplia terraza de la pirámide y alzadas con palancas y cortos planos inclinados o grandes balancines, sobre cada nueva hilada hasta alcanzar el nivel de colocación que les correspondía.
   </div>
   <div class="paragraph">
      En adelante la rampa se mantendrá invariable hasta la finalización de la obra; pero, su función elevadora, será transferida a un dispositivo emplazado sobre gradas de la pirámide y a la acción de un complejo mecanismo situado en las cámaras interiores del que, a su vez, formaban parte.
   </div>
   <div class="paragraph">
      Es decir que, lo construido en la primera fase, fue un tronco de pirámide cortado por la prolongación del plano de la propia rampa, de 50,441 m de altura en la cara sur y 57,562 m en la fachada norte. 
   </div>
