   <div class="separator"></div>
   
   <div class="subsubtitle">
      El torno reductor
   </div>
   <div class="paragraph">
      La cadena procedente del Pasaje Ascendente, antes de regresar al engranaje situado sobre el Gran Peldaño para cerrar el circuito, penetraba en el interior de la Cámara de la Reina donde había emplazado un gran torno provisto de volante; mecanismo muy necesario para disponer de un efectivo control en el desplazamiento y parada de los contrapesos. El torno es parte fundamental en el mecanismo. Su efecto reductor, por acoplamiento de engranajes, proporciona (a cambio de velocidad) una considerable fuerza tanto para ayudar en la fase de retorno de los contrapesos a la zona de partida, como en el control de los mismos durante su descenso; dado que, una vez vencido el roce estático, los trineos, experimentarían una aceleración que había de ser controlada a fin de aminorar su impacto contra los topes de la galería. También evitaría los daños que se derivarían de un posible desprendimiento del sillar durante la fase de izado <font class="mark_text">*(Fig.6)</font>.
   </div>
   <div class="paragraph">
      Puesto que los tres contrapesos habían de intervenir de forma correlativa pero independiente, era necesario disponer de un mecanismo que accionara el acoplamiento y desacoplamiento de los trineos a la cadena motriz. Las cuerdas de accionado de las mordazas de enganche deberían permanecer siempre en su justa tensión; esta era la importante misión de la Antecámara.
   </div>
   <div class="gallery_border">
      <a class="fancybox-thumb" rel="gallery3" href="../../img/section4/fig6.jpg" title="Figura 6. La Cámara de la Reina con la cabria reductora y los contrapesos en zona de mantenimiento">
         <img src="../../img/section4/fig6-min.jpg" alt="Figura6" />
      </a>
   </div>
   
   <div class="separator"></div>
   
   <div class="subsubtitle">
      Dispositivo exterior  
   </div>
   <div class="paragraph">
      En el exterior, las piedras ascendían montadas sobre patines y suspendidas de un travesaño que se deslizaba sobre dos barandas de madera paralelas que, a modo de rieles, se prolongaban manteniendo una cierta distancia respecto del revestimiento de la fachada. Eran sustentadas por sólidos postes de madera apoyados sobre las gradas formadas por la ausencia de uno de cada tres bloques del recubrimiento de la propia pirámide. Toda la estructura se iba prolongando por módulos a medida que la obra ganaba altura. El extremo motriz se trasladaba, una vez finalizada la terraza o hilada, a la grada superior y, el espacio libre dejado por aquel, se suplementaba con un nuevo tramo de barandas <font class="mark_text">*(Fig. 7 y 8)</font>.
   </div>
   <div class="paragraph">
      Después de descargar la piedra sobre la pirámide, había que sustituirla por algo que tirara de los contrapesos de la galería haciéndolos regresar a su nivel de partida; es decir, junto al Gran Peldaño. 
   </div>
   <div class="paragraph">
      La ascensión de los trineos-contrapeso se conseguía llenando de arena el espacio antes ocupado por la piedra; aunque con un peso superior al de esta.
   </div>
    <div class="paragraph">
      Dos hileras de obreros situados sobre gradas de madera, a ambos lados de las barandas, formando una doble cadena humana, iban trasladando y ascendiendo pequeños sacos llenos de arena, unidos entre sí por medio de una cuerda. La arena utilizada era siempre la misma. Se descargaba de la plataforma de ascensión, al llegar a la parte inferior, y se la volvía a izar rápidamente tirando de la soga; al tiempo que, otros operarios, procedían a cargar y ascender una nueva piedra. De esta forma, cuando el sillar era depositado en la terraza para su colocación, la arena ya estaba dispuesta para reemplazarlo.
   </div>
   <div class="paragraph">
      Aproximadamente 1.000.0000 bloques o sillares fueron así colocados. Calculo que la pirámide tendría en total, incluyendo el piramidión, 217 hiladas de mampostería que fueron construidas de la siguiente forma:
   </div>
   <div class="paragraph">
      Las primeras 61 y parte de la 62 a la 71, mediante el procedimiento de rampa o plano inclinado, emplazado sobre la cara sur. La parte complementaria de las hiladas 62 a 71, más las 146 restantes, por medio del mecanismo de las cámaras interiores. 
   </div>
   <div class="paragraph">
      El primer contrapeso se encargaría de izar los sillares de 45 hiladas; en otras palabras, completaría la pirámide hasta el nivel superior de la hilada 106. El segundo contrapeso, junto con el primero, colocaría las 52 siguientes y la dejaría truncada en la 158. Por último, intervendría el tercer contrapeso que remataría la pirámide con las 59 hiladas restantes, mucho más pequeñas en su mayoría, a excepción posiblemente de la última o piramidión. 
   </div>
   <div class="paragraph">
      El volumen total de la pirámide era de 2.591.701,6 m³. El volumen de la parte destinada al mecanismo de las cámaras: 650.007,45 m³ <font class="mark_text">*(Fig.9)</font>. 
   </div>
