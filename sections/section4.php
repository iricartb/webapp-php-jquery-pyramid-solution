   <div class="subsubtitle">
      Terraza auxiliar   
   </div>
   <div class="paragraph">
      Por este sistema de construcción es posible deducir que, el recubrimiento exterior, de la pirámide se colocó al mismo tiempo que el núcleo de mampostería y una hilada por delante de este, a excepción de las cavidades ocupadas por los postes de sustentación de las barandas de ascensión. Esta zona se completó de arriba hacia abajo (tal como los sacerdotes egipcios relataron a Herodoto) al tiempo que iban desmontando todo el armazón del sistema elevador en el exterior. 
   </div>
   <div class="paragraph">
      Las piedras de revestimiento eran arrastradas sobre la terraza en construcción y colocadas formando el reborde perimetral; posteriormente se llenaba el espacio interior con sillares de respaldo y núcleo. Esto permitía trabajar <i>"desde dentro"</i> en la colocación y ajuste milimétrico de las piedras. De otra forma, hubiera sido imposible conseguir acoplarlas con tanta precisión. 
   </div>
   <div class="paragraph">
      No obstante, para la colocación de las últimas hiladas y el piramidión, hubo de construirse una terraza lateral adosada sobre la fachada este, que permitiera operar desde fuera, puesto que la superficie de trabajo se reduce progresivamente a medida que se accede a la cumbre. Es por ello que el conducto del respiradero sur se halla desplazado de la apotema de la pirámide 10 Codos, o 5,3 m., hacia el lado este <font class="mark_text">*(Fig.10)</font>. 
   </div>
   <div class="paragraph">
      El trabajo empleado en la construcción de la terraza auxiliar era recuperado al final, durante la fase de desmonte de la misma; al utilizar sus propios bloques como lastre en sustitución de la arena que, de otra forma, hubiera sido necesario subir para lograr el regreso de los contrapesos durante la operación de izado del revestimiento correspondiente a esta parte de la pirámide.
   </div>
