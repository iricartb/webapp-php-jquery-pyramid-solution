   <div class="separator"></div>
   
   <div class="subtitle">
      4.2 El mecanismo de las cámaras
   </div>
   <div class="paragraph">
      La disposición y funcionamiento del sistema que permitía izar las piedras a partir del plano inclinado o rampa era el siguiente:
   </div>
   <div class="paragraph">
      Los tres grandes bloques de granito que forman la cuña obstructora, encajados en el inicio del Pasaje Ascendente, estaban situados en el extremo superior de la Gran Galería, inmediatamente después del Gran Peldaño, montados sobre altos trineos de madera.
   </div>
   <div class="paragraph">
      En cuanto soltaban los anclajes que les retenían, los trineos, se precipitaban por la pendiente de la galería accionando una larga cadena de transmisión que, a su vez, producía (por acoplamiento mecánico) un movimiento de giro al rodillo emplazado en el interior de la Cámara del Rey.
   </div>
   <div class="paragraph">
      El rodillo de la cámara real, engranaba con otro que tenía por misión rebobinar una gruesa cuerda que penetraba por el conducto o respiradero de la cara sur. Esta soga cruzaba el núcleo de mampostería de la pirámide y salía al exterior para unirse al aparato elevador de sillares situado al final de la rampa o zona de llegada de las piedras.
   </div>
   <div class="paragraph">
      Es decir que, la fuerza de arrastre necesaria para izar los sillares en el exterior, la proporcionaba la fuerza de resbalamiento de los contrapesos al deslizarse a través de la Gran Galería <font class="mark_text">*(Fig.3)</font>.
   </div>
   <div class="paragraph">
      La Gran Galería está provista en toda su longitud y en ambas ménsulas, de 27 huecos o muescas, distribuidas de forma que dejan espacios regulares entre sí. La misión de estas cavidades era la de anclar unos postes de madera que, llegando en altura hasta el segundo saliente de la pared, formaban una estructura capaz de resistir el impacto de los trineos contrapeso contra los topes de contención distribuidos a lo largo de una baranda adosada a esta estructura.
   </div>
   <div class="paragraph">
      Los contrapesos se utilizaban por separado y de forma correlativa. A cada hilada o nivel de construcción, le correspondía una determinada secuencia de topes en la galería.
   </div>
   <div class="paragraph">
      Para la colocación de las primeras hiladas, a partir de la rampa exterior, se empleó únicamente el primer contrapeso; este iba parando un tope más bajo o lejano a medida que se completaban las terrazas, hasta agotar la longitud de la galería. Cuatro hiladas por encima del nivel de salida del respiradero entraba en funcionamiento, además del primero, el segundo contrapeso; ahora ambos compartían el recorrido necesario. Solamente en la colocación de las últimas hiladas intervenía, junto con los otros dos, el tercer contrapeso <font class="mark_text">*(Fig.4)</font>.
   </div>
   <div class="paragraph">
      El recorrido necesario para llegar al vértice de la pirámide, a partir de la rampa exterior, es exactamente tres veces la longitud útil o disponible para el desplazamiento de cada trineo en el interior de la galería: 40,754 m <font class="mark_text">*(Fig.5)</font>.
   </div>
   <div class="paragraph">
      El circuito que recorría el mecanismo elevador en el interior de la pirámide, ocupaba todas las cámaras y galerías de la misma; excepto la subterránea. Su extremo más apartado alcanzaba la confluencia del Pasaje Ascendente con el Pasaje Descendente; zona ocupada hoy por la cuña de granito obstructora. En este punto había una rueda dentada que invertía el sentido de la cadena que procedía de la galería. 
   </div>
