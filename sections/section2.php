   <div class="subtitle">
      2.10 La Cámara Subterránea
   </div>
   <div class="paragraph">   
      La estancia mide 8,36 m de norte a sur por 14,06 m de este a oeste. Su máxima altura es de 4,3 m El esculpido de su techo y paredes se dejó perfectamente acabado; no así el suelo que presenta grandes protuberancias rocosas, surcos e incluso un pozo que fue agrandado por el coronel Vyse. La estancia, es tan exageradamente irregular que ha dado pie a fantásticas interpretaciones esotéricas. Aunque se desconocen los motivos, la opinión más aceptada es que, el aspecto actual, se debe al abandono de forma repentina de los trabajos de excavación <font class="mark_text">*(Fig.5)</font>.
   </div>
   <div class="paragraph">   
      De esta cámara parte en dirección sur y sobre la cota -30,07 m, un conducto ciego, de trazo irregular. Queda interrumpido bruscamente a los 16,41 m Probablemente el propósito sería construir dos cámaras subterráneas unidas entre sí por un corto pasaje tal como presenta la pirámide Roja en Dahshur.
   </div>
   <div class="gallery_border">
      <a class="fancybox-thumb" rel="gallery5" href="../../img/section2/fig5.jpg" title="Figura 5. Cámara Subterránea">
         <img src="../../img/section2/fig5.jpg" alt="Figura5" />
