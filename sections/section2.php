   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      2.11 Pasaje Ascendente
   </div>
   <div class="paragraph">   
      En la parte superior o techo del Pasaje Descendente, se halla el inicio del Pasaje Ascendente; obstruido en este punto por tres grandes bloques de duro granito <font class="mark_text">*(Fig.6)</font>. Ambos pasajes tienen las mismas dimensiones y se bifurcan mutuamente adoptando un ángulo de 26º 18' 10" respecto a la horizontal, según Edgar. Según Petrie el ángulo del Pasaje Ascendente es de 26º 2' 30" <font class="mark_text">[PETRIE, 1990, p. 22]</font>. La proyección del piso del Pasaje Ascendente sobre el piso del Descendente quedaba a 28,22 m de la verdadera entrada.
   </div>
   <div class="paragraph">   
      Gracias a la excavación de los árabes es posible rodear la cuña obstructora y acceder a las cámaras superiores. El tapón granítico tiene una longitud de 4,54 m aunque se supone que originalmente alcanzaría los 5,15 m dado que el tercer bloque aparece quebrado y por ello de menor longitud. El primer bloque tiene forma puntiaguda en su parte delantera, reduciendo su anchura a 97 cm en lugar de los 105 cm de su parte posterior. El objeto de esta reducción es adaptarse al constreñimiento que presenta el extremo inferior del pasaje, haciendo el sellado más sólido y estanco.
   </div>
   <div class="paragraph">   
      El piso del Pasaje Ascendente, con una longitud de 39,28 m, a partir de su proyección sobre el Pasaje Descendente, desemboca en un espacio que es confluencia de la espléndida Gran Galería y el corredor llano que concluye en una segunda estancia: la Cámara de la Reina.
   </div>
   <div class="gallery_border">
      <a class="fancybox-thumb" rel="gallery6" href="../../img/section2/fig6.jpg" title="Figura 6. Pasaje ascendente">
         <img src="../../img/section2/fig6.jpg" alt="Figura6" />
      </a>
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      2.12 Corredor de la Cámara de la Reina
   </div>
   <div class="paragraph">   
      El pasillo que conduce a esta cámara tiene solamente 1,17 m de alto pero, cuando se han recorrido 33,2 m y faltando 5,5 m para acceder a la estancia, se presenta un escalón y la altura se incrementa en 50 cm <font class="mark_text">*(Fig.7)</font>. Este corredor tiene algunas peculiaridades dignas de ser remarcadas. En primer lugar, es horizontal únicamente en 1,32 m. a partir de la pared norte. Prosigue con leve desnivel (hacia la cámara) que culmina en el escalón con un valor total de 10 cm <font class="mark_text">[PETRIE, 1990, p.22]</font>. En segundo lugar, las paredes están formadas por sillares con juntas coincidentes. Esta insólita disposición llevó a los arquitectos franceses Gilles Dormión y J. Patrice Goiden, a solicitar permiso de las autoridades egipcias para practicar tres agujeros en los sillares de la pared oeste. La operación se llevo a cabo en 1986 con éxito relativo, dado que se localizaron espacios supuestamente vacíos que contenían una arena especial formada en su mayor parte por cuarzo, mezclada con cloruro de sodio <font class="mark_text">[DORMION & GOIDIN, 1987] [KERISEL, 1991, p.70]</font>.
   </div>
   <div class="paragraph">   
      En 1987 la existencia de estas cavidades y otra posterior más próxima a la Cámara de la Reina, fue detectada por medio de ondas electromagnéticas en unas pruebas de sondeo en toda la pirámide, efectuadas por la Universidad de Waseda (Japón) bajo la dirección de Sakuji Yoshimura <font class="mark_text">[YOSHIMURA, 1988]</font>.
   </div>
   <div class="paragraph">   
      J.Kerisel, sin hacer mención de las investigaciones llevadas a cabo por los japoneses, opina que del resultado de los sondeos no debería inferirse la existencia de grandes espacios vacíos o cámaras ocultas, sino simplemente zonas de menor densidad. 
   </div>
   <div class="paragraph">   
      Las mediciones microgravimétricas efectuadas por medio de un péndulo extremadamente sensible, indican que por encima del techo de la Gran Galería y localmente en el lado oeste del corredor a la Cámara de la Reina, la densidad se sitúa entre 1,8 y 1,9 Kg/dm³ <font class="mark_text">[KERISEL, 1991, p.67]</font>. 
   </div>
   <div class="gallery_border">
      <a class="fancybox-thumb" rel="gallery7" href="../../img/section2/fig7.jpg" title="Figura 7. Cámaras superiores: Corredor a la Cámara de la Reina, Cámara de la Reina, Gran Galería, Antecámara y Cámara del Rey con su estructura de techos">
         <img src="../../img/section2/fig7-min.jpg" alt="Figura7" />
      </a>
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      2.13 Cámara de la Reina
   </div>
   <div class="paragraph">   
      El piso que conduce a la Cámara de la Reina, se halla a 21,206 m sobre la base de la pirámide; antes del escalón 21,71 m, aproximadamente la misma distancia que la separa del piso de la tercera y superior. La sala mide 5,23 m en sentido norte-sur y 5,76 en su eje este-oeste. El techo está formado por 6 gabletes de piedra caliza con una doble pendiente de 30º 14' y una altura máxima de 6,23 m. Algunos investigadores apuntan la posibilidad de que sobre este techo y por encima del último de la Cámara del Rey, haya otros niveles de vigas a contraste, tal como se observa en las cámaras funerarias de las pirámides de la VI dinastía en Saqqara <font class="mark_text">[GOYON, 1990, pp. 203-219]</font>. 
   </div>
   <div class="paragraph">   
      El suelo parece como si lo hubieran dejado sin acabar, a falta de un posterior revestimiento. Stadelmann ha sugerido que el piso de la estancia podría constituirlo el macizo rocoso de la propia meseta.
   </div>
   <div class="paragraph">   
      Su pared este presenta una cavidad formada por cinco secciones telescópicas, llamada Nicho, desplazado 0,64 m del eje de simetría de la estancia que, a su vez, coincide con el eje de simetría este-oeste de la pirámide.
   </div>
   <div class="paragraph">   
      Su interior se excavó hasta una longitud de 15,3 m con la esperanza de hallar algún nuevo y oculto pasaje. Se desconoce su función. Petrie creía que su finalidad era albergar una estatua de diorita; lo mismo opina Goyon:
   </div>
   <div class="paragraph">   
      <i>"En el muro este, un nicho, con una profundidad en origen de 1 m, debía contener una estatua; probablemente de diorita"</i> <font class="mark_text">[GOYON, 1990, p.203]</font>. 
   </div>
   <div class="paragraph">   
      Para Petrie, las dimensiones del nicho demuestran que la pirámide fue construida en base a un patrón de medida o Codo de 20,6"(52,3 cm) dado que, así, sus medidas serían exactamente de 3 codos en la base y cuatro sucesivas reducciones de 1/4 de codo en ambos lados por el recurso de saledizo de los sillares que forman la pared de la cámara; esto dejaría un codo de anchura para la última sección. La profundidad del habitáculo sería de 2 codos <br/><font class="mark_text">[PETRIE, 1990, p.24]</font>. 
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      2.14 Respiraderos de la Cámara de la Reina
   </div>
   <div class="paragraph">   
      Sus paredes norte y sur, a la altura de la entrada, están provistas de sendos conductos estrechos, 22 cm, cuya trayectoria se vuelve ascendente a partir de 1,93 m con un ángulo, según Petrie, de 37º 28' el norte y a 2,03 m con un ángulo de 38º 28' el que se dirige hacia el sur <font class="mark_text">[PETRIE, 1990, p.24]</font>.
   </div>
   <div class="paragraph">   
      Estos conductos llamados respiraderos fueron descubiertos en 1872 por Waynman Dixon. Su existencia había pasado inadvertida dado que su embocadura permanecía oculta tras escasos centímetros de piedra del revestimiento de la propia cámara. W. Dixon y el Doctor Grant improvisaron una pequeña hoguera en el interior del tramo llano de estos canales con la esperanza de localizar, por medio del humo, sus correspondientes salidas al exterior. El experimento resultó infructuoso.<br/>En el interior del conducto norte, Dixon, según testimonio de Piazzi Smyth halló tres desconcertantes objetos: un pequeño y doble gancho de bronce, una porción de madera de cedro con muescas y una bola de granito gris de 8.325 granos; aproximadamente 540 gramos <font class="mark_text">[SMYTH, 1978, pp. 428-429]</font>. 
   </div>
   <div class="paragraph">   
      En 1993, se reanudó el proyecto de mejora del sistema de ventilación iniciado el año anterior. En aquella ocasión se instalaron extractores de aire en los canales o respiraderos de la Cámara del Rey. Esta vez se trabajó sobre los conductos de la Cámara de la Reina. Hasta entonces solamente se habían explorado los 8 m iniciales y existían serias dudas respecto a su verdadera longitud pues, todavía hoy, se desconocen sus respectivas salidas al exterior. A través del conducto que parte de la pared sur se introdujo un sofisticado robot, construido por el ingeniero alemán Rudolf Gantenbrink que, inesperadamente, recorrió casi 60 m antes de detenerse obligado por la presencia de una piedra lisa provista de dos pequeños pomos o tiradores metálicos que, a modo de compuerta, obstruye el conducto a este nivel <font class="mark_text">*(Fig.8)</font>.
   </div>
   <div class="paragraph">   
      El 17 de septiembre de 2002, un nuevo robot denominado Pyramid Rover, construido por la empresa <i>"iRobot"</i> y patrocinado por el Consejo Supremo de Antigüedades de Egipto y National Geographic Channel, perforó la losa descubierta por Gantenbrink constatando que, más allá de la misma, únicamente hay un espacio vacío algo mayor que el del propio conducto y que, a unos 18-19 cm de distancia, se divisa un nuevo bloque de piedra liso que obstruye totalmente el paso <font class="mark_text">*(Fig.9)</font>.
   </div>
   <div class="paragraph">   
      El respiradero norte experimenta un cambio brusco de trayectoria, inclinándose hacia el oeste, eludiendo La Gran Galería. Debido a este irregular trazado y a la presencia de una barra metálica de sondeo, abandonada en su día por W.Dixon, el robot de R.Gantenbrink no pudo explorarlo.
   </div>
   <div class="paragraph">   
      Pero el 23 de Septiembre de 2002; es decir, una semana después de la perforación de la losa del canal sur, el Pyramid Rover descubría la existencia de una <i>"puerta"</i> similar a la del canal opuesto, provista de sendos tiradores de cobre o bronce, situada a 215 pies (unos 65m.) de distancia. Se ignora que hay más allá de este obstáculo.
   </div>
   <div class="paragraph">   
      Según Gantenbrink, el ángulo respecto a la horizontal de estos conductos difiere del calculado por Petrie y es: 33º 18' y 40º 6' en el respiradero norte, y 39º 36' 28,08" en el respiradero sur.
   </div>
   <div class="paragraph">   
      En 2010 un equipo internacional denominado Djedi, patrocinado por la Universidad de Leeds, consiguió, mediante la introducción de otro robot, fotografiar el espacio posterior de la losa y el dorso de la misma, disipando finalmente las dudas sobre si los tiradores metálicos eran independientes o si por el contrario estaban unidos formando algún tipo de asidero <font class="mark_text">*(Fig. 10)</font>. 
   </div>
   <div class="paragraph">   
      Según las imágenes facilitadas por ASAE (Annales du Services des Antiquities de l’Égypte, vol. 84, 2010), aunque no son muy nítidas, se puede afirmar que cada uno de los tiradores forma un bucle o anillo en su parte posterior.
   </div>
