   <div class="subtitle">
      2.8 Accesos a su interior
   </div>
   <div class="paragraph">   
      La entrada a las cámaras interiores se efectúa hoy a través de la brecha supuestamente abierta por Al-Mamún en el siglo IX después de Jesucristo, quebrando y desalojando los sillares a nivel de la séptima hilada y sobre la apotema de la fachada norte <font class="mark_text">*(Fig.3)</font>; no obstante, la verdadera entrada de la pirámide, se correspondería con la hilada 19 del desaparecido revestimiento y a 16,97 m. de la base.
   </div>
   <div class="paragraph">   
      Esta pequeña entrada desplazada del eje de simetría 7,29 m., está protegida por grandes placas-dintel y un doble gablete de inmensas piedras cuyas dimensiones no parecen estar justificadas en base a una exclusiva función de descarga del peso de los sillares que gravitan sobre el angosto pasaje.
   </div>
   <div class="paragraph">   
      Es posible, además, que esta protección de vigas a contraste se dé a todo lo largo del corredor descendente; tal como se observa en la pirámide de Neferirkare en Abusir, dado el estado ruinoso de este monumento, en la actualidad <font class="mark_text">[MARAGIOGLIO & RINALDI, 1965, p.111 obs.12]</font>.
   </div>
   <div class="paragraph">   
      Según Estrabon, la entrada estaba provista de una piedra giratoria que actuaba a modo de puerta basculante y daba acceso al largo y profundo pasaje que conduce a la cámara subterránea <font class="mark_text">[ESTRABON, 1997, p.139]</font>. 
   </div>
   <div class="paragraph">   
      Petrie acepta este testimonio dado que en la pirámide Romboidal de Dahshur, hay indicios de haber dispuesto de un mecanismo semejante para acceder al pasaje descendente de la cara norte.
   </div>
