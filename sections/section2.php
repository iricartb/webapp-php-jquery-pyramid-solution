   <div class="subtitle">
      2.7 Estructura interna
   </div>
   <div class="paragraph">   
      Se compone de tres cámaras principales y otra menor llamada Antecámara. La mayor es subterránea, excavada en la roca viva del subsuelo y las dos restantes se hallan emplazadas a diferente nivel en el núcleo de mampostería. Todas ellas se hallan conectadas entre si por medio de tres angostos pasajes y una gran galería. Un estrecho e irregular pozo comunica a modo de atajo, el extremo inferior del pasaje descendente con las cámaras superiores, en el punto de confluencia del pasaje ascendente, el corredor que conduce a la segunda cámara y la gran galería <font class="mark_text">*(Fig.2)</font>.
   </div>
