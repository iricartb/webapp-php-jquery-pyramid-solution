   <div class="subtitle">
      2.15 Zona de confluencia de los tres pasajes
   </div>
   <div class="paragraph">   
      Regresando a la zona de confluencia de los tres pasajes, observamos la presencia en ambas paredes de 5 cavidades de forma trapezoidal alineadas siguiendo la trayectoria que une el piso de la Gran Galería con el Pasaje Ascendente. Dichos huecos tienen diferente profundidad alternada y el segundo, que ocupa una posición aproximadamente central, es mucho mayor que los restantes. Se supone que contenían viguetas para sostener una plataforma que impedía el paso a la Cámara de la Reina y facilitaba el acceso al piso de la Gran Galería. 
   </div>
   <div class="paragraph">   
      Maragioglio y Rinaldi confiesan no disponer de una explicación satisfactoria que justifique la diferencia de medidas y su alternancia <br/><font class="mark_text">[MARAGIOGLIO & RINALDI, 1965, IV, p.41]</font>. 
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      2.16 La Gran Galería
   </div>
   <div class="paragraph">   
      Es una obra maestra de arquitectura y asombra tanto por sus dimensiones como por la pericia con que fue realizada. Es prolongación del estrecho Pasaje Ascendente pero, a diferencia de este, su acimut es ligeramente diferente: + 1' 20"(Petrie). De ancho mide 2,09 m y, lo más asombroso, el techo se eleva en vertical a 8,48 m. Sus paredes, formadas por inmensas piedras prismáticas, componen una bóveda telescópica por el recurso arquitectónico basado en hacer que siete ménsulas, en ambas paredes, a partir de 1,8 m vayan sobresaliendo unas pulgadas a medida que se gana altura. <font class="mark_text">*(Fig.9)</font> 
   </div>
   <div class="paragraph">   
      El techo resultante de este acercamiento es la mitad del ancho de la galería en su parte inferior. Treinta y siete jácenas dispuestas a modo de diente de sierra, conforman esta singular techumbre.
   </div>
   <div class="paragraph">
      El empleo del saledizo en las hiladas, como recurso para formar falsas bóvedas, ya fue utilizado por Esnofru en las cámaras interiores de sus pirámides emplazadas en Dahshur: Romboidal y Roja. Incluso la pirámide de Meidum, anterior a todas ellas, adopta para su cámara funeraria la misma disposición.
   </div>
   <div class="paragraph">
      El suelo de la Gran Galería, a modo de rampa, con un pasillo central de 1,05 m de ancho y 46,12 m de largo, presenta largas y estrechas entalladuras transversales que se supone fueron practicadas con posterioridad para facilitar la escalada. Está flanqueado por ménsulas de 52 cm de altura y anchura. Estos altos escalones laterales presentan, en toda su longitud y a espacios regulares, 27 cavidades rectangulares, alternativamente largas y cortas. A cada una de estas oquedades (52 y 59 cm de longitud, 14 cm de ancho y 18 cm de profundidad) le corresponde otra excavada en la pared formando un pequeño nicho que, a su vez, se halla provisto de una piedra o mojón inserto. Unas no muy profundas entalladuras, paralelas al piso de la galería, cruzan e incluso rebasan en algo a los mojones. Solamente los dos primeros agujeros, en el extremo inferior de la galería, carecen de nicho y el primer nicho de entalladura.
   </div>
   <div class="paragraph">
      Ni arqueólogos, ni técnicos, hasta ahora, han sabido dar una explicación convincente de la función específica de estas cavidades <br/><font class="mark_text">[MARAGIOGLIO & RINALDI, 1965, IV, p.39 obs.17]</font>.
   </div>
   <div class="paragraph">
      Una ranura esculpida recorre, en toda su longitud, las dos paredes de la galería a nivel del tercer saliente.
   </div>
   <div class="paragraph">
      El pasillo central finaliza, en su extremo superior, en un alto escalón (90 cm.) muy deteriorado, o esculpido, conocido por Gran Peldaño. Sobre esta plataforma o descansillo y en ambos rincones, se observa también la presencia de un hueco de similares características a los que taladran las ménsulas de la galería; aunque desprovisto del correspondiente nicho. 
   </div>
   <div class="paragraph">
      Partiendo del Gran Peldaño y a través de un conducto de 1,11 m de altura por 1,05 m de ancho, se accede a una sala del mismo ancho y 2,96 m de longitud, llamada Antecámara.
   </div>
   <div class="gallery_border">
      <a class="fancybox-thumb" rel="gallery9" href="../../img/section2/fig11.jpg" title="Figura 11. La Gran Galería vista desde el Gran Peldaño">
         <img src="../../img/section2/fig11.jpg" alt="Figura11" />
      </a>
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      2.17 Antecámara
   </div>
   <div class="paragraph">   
      Sus dos paredes laterales, de granito, tienen esculpidos cuatro rebajes verticales. El primero, de 30 cm de ancho, hace la función de guía de dos misteriosas losas de granito; la inferior es fija y forma un falso dintel a continuación de la entrada. La superior, desplazable, presenta una protuberancia llamada sello. Ambas losas disponen de un pequeño taladro en su parte posterior, mutuamente alineados.
   </div>
   <div class="paragraph">   
      Los tres restantes rebajes, de 55 cm de anchura, se prolongan unas tres pulgadas por debajo del piso de la estancia (de granito en su mayor parte) y finalizan en la pared oeste a una altura de 2,86 m, con huecos esculpidos en forma semicircular de aproximadamente 38 cm de diámetro, muy desgastados <font class="mark_text">*(Fig.10)</font>. La pared este no es simétrica respecto de la oeste puesto que carece de las entalladuras semicirculares y los rebajes acaban de forma llana a una altura de 2,61 m. 
   </div>
   <div class="paragraph">   
      La pared sur de esta cámara, también de granito a excepción de una estrecha franja de piedra caliza en su parte superior, está surcada verticalmente por cuatro regatas de unos 10 cm de diámetro y separadas por espacios regulares.
   </div>
   <div class="paragraph">   
      El diseño de todas estas canaladuras en la antesala de una cámara funeraria, sugiere a los investigadores una posible función de guía para accionar rastrillos o compuertas destinadas a impedir el acceso a la misma. (Esta hipótesis será analizada al abordar la teoría funeraria)
   </div>
   <div class="paragraph">   
      Otra puerta similar a la anterior y con una longitud de 2,56 m, nos permite abandonar la Antecámara en dirección sur y penetrar en la mayor y más sugestiva sala de la pirámide: La Cámara del Rey.
   </div>
   <div class="gallery_border">
      <a class="fancybox-thumb" rel="gallery10" href="../../img/section2/fig12.jpg" title="Figura 12. Entalladuras semicirculares">
         <img src="../../img/section2/fig12.jpg" alt="Figura12" />
      </a>
   </div>

   <div class="separator"></div>
   
   <div class="subtitle">
      2.18 La Cámara del Rey
   </div>
   <div class="paragraph">   
      La cámara, considerada sepulcro violado del faraón Quéope, mide 10,48 m de este a oeste y 5,24 m de norte a sur. Su altura alcanza los 5,84 m <font class="mark_text">*(Fig.7)</font>.
   </div>
   <div class="paragraph">   
      Es una magnífica estancia construida en su totalidad con grandes bloques de granito rojo, procedentes de las canteras de Asuán; de considerables dimensiones y ajustados con toda precisión. El dintel de la puerta o entrada, lo forma un monolítico bloque que mide: 3,47 m de largo y 2,17 m de alto. Una estrecha franja del mismo se introduce o encastra en la pared este <font class="mark_text">*(Fig.13)</font>.
   </div>
   <div class="paragraph">   
      Las mediciones de Petrie han revelado que el suelo de la estancia no es completamente plano; tampoco forman ángulo recto las paredes con el techo y el suelo, la diferencia es del orden de 5' de grado <font class="mark_text">[PETRIE, 1990, p.27]</font>. En opinión de los arquitectos italianos Maragioglio y Rinaldi, la mayor parte de estas diferencias se deben a un asentamiento del núcleo de mampostería. Las variaciones de nivel en el piso oscilan entre 2,5 y 5 cm; hallándose el mayor desnivel en el lado sur, a -3,3 cm de la media. 
   </div>
   <div class="paragraph">   
      Kerisel dice que el muro sur descendió una media de 4 cm alcanzando los 6 cm en la parte central y algo menos en las esquinas este y oeste debido a la unión de los planos <font class="mark_text">[KERISEL, 1991, 70]</font>. 
   </div>
   <div class="paragraph">   
      Otra peculiaridad de esta estancia es que las paredes se asientan directamente sobre los sillares de piedra caliza del núcleo, en lugar de hacerlo sobre las losas de granito que conforman el piso. Las paredes quedan 15 cm por debajo del piso de la cámara. En la parte inferior de los bloques que constituyen la primera hilada de las paredes se dejaron unas protuberancias que fueron esculpidas en obra y durante la fase de colocación del piso <br/><font class="mark_text">[MARAGIOGLIO & RINALDI, 1965, IV, p.49 observ.29]</font>.
   </div>
   <div class="gallery_border">
      <a class="fancybox-thumb" rel="gallery11" href="../../img/section2/fig13.jpg" title="Figura 13. Respiradero norte, entrada y el gran dintel sobre la puerta">
         <img src="../../img/section2/fig13-min.jpg" alt="Figura13" />
      </a>
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      2.19 Respiraderos de la Cámara del Rey
   </div>
   <div class="paragraph">   
      En la pared sur, a nivel de la entrada y a 2,48 m. de la esquina SE, destaca un gran y profundo boquete de forma oval <font class="mark_text">*(Fig.14)</font>. 
   </div>
   <div class="paragraph">   
      John Greaves en su exploración de la pirámide en 1638, había reparado en él; no obstante, desconocía su naturaleza y lo atribuyó a un receptáculo para acomodar lámparas. Fue el coronel Howard Vyse quien descubrió en 1837 que en realidad se trataba de un estrecho conducto que con ángulo ascendente cruzaba toda la masa de sillería hasta comunicar con el exterior a nivel de la hilada 102 del núcleo. Sus medidas son de 23 x 22,5 cm y su ángulo de pendiente es de 45º 13' 40" según la media de diversas ediciones efectuadas por Petrie <font class="mark_text">[PETRIE, 1883, cap.7-56] y [PETRIE, 1990, p.29]</font>. No obstante, Gantenbrink, obtuvo con su robot: 45º 00". 
   </div>
   <div class="paragraph">   
      En 1837 el ingeniero J.R. Hill, al servicio del coronel Howard Vyse, halló una placa de hierro incrustada en la piedra de este conducto, cerca del exterior. 
   </div>
   <div class="paragraph">   
      Maragioglio y Rinaldi creen que el desboco que presenta en su inicio, se debe a exploraciones posteriores. Por el contrario, Edgar, cree que tanto el desboco que alcanza los 70 cm de altura como el doble codo interior con su forma oval fue estucada por los egipcios y formaba parte del diseño original.
   </div>
   <div class="paragraph">   
      Este conducto tiene un homólogo en la pared norte aunque de muy distinta abertura y trayectoria. Es llano a lo largo de los primeros 2,63 m, para ascender girando a NO <font class="mark_text">*(Fig.13)</font>.
   </div>
   <div class="paragraph">   
      Presenta tramos de diferente pendiente (ángulo medio de Petrie 31º 33', Gantenbrink entre 32º 36' y 31º 12'), hasta irrumpir a nivel de la hilada 101 del núcleo de mampostería. 
   </div>
   <div class="gallery_border">
      <a class="fancybox-thumb" rel="gallery12" href="../../img/section2/fig14.jpg" title="Figura 14. Embocadura del respiradero sur">
         <img src="../../img/section2/fig14-min.jpg" alt="Figura14" />
      </a>
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      2.20 Conjunto de techos 
   </div>
   <div class="paragraph">   
      El primer techo de la cámara lo forman nueve monolíticos bloques de granito con su cara inferior lisa y colocados en sentido norte-sur. El peso de cada uno de estos arquitrabes oscila entre cincuenta y setenta toneladas <font class="mark_text">[KERISEL, 1996]</font>. 
   </div>
   <div class="paragraph">   
      Sobre este techo, existen otros cuatro de similares características; aunque el segundo y el quinto lo forman 8 vigas, en lugar de 9. Todos ellos separados entre sí por espacios vacíos llamados cámaras de descarga, a pesar de que los arquitectos no han sabido dar una explicación científica a tan peculiar disposición. Kerisel dice al respecto:
   </div>
   <div class="paragraph">   
      <i>"Nada más inadecuado que la expresión tan usual de cámaras de descarga; al contrario, son cámaras de sobrecarga"</i> <font class="mark_text">[KERISEL, 1996, p. 256]</font>.
   </div>
   <div class="paragraph">   
      Todo este conjunto de techos planos está rematado por once pares de grandes piedras calizas en forma de gablete o tejadillo que absorben el peso de los sillares que restan hasta la cumbre y lo descargan hacia los flancos; evitando así que graviten sobre el espacio que ocupa la cámara <font class="mark_text">*(Fig. 15 y 16)</font>. 
   </div>
   <div class="paragraph">   
      La primera cámara de descarga fue descubierta por Nathaniel Davison en 1765, a partir de un estrecho e irregular conducto que nace en el extremo superior de la Gran Galería, junto a la pared oriental.
   </div>
   <div class="paragraph">   
      Es prácticamente seguro que este pasaje fue excavado por los propios egipcios durante la construcción de la pirámide para comprobar la gravedad de las fisuras en las vigas. Únicamente alguien que conociera con total exactitud el diseño de los techos podía excavar un túnel en la dirección precisa. 
   </div>
   <div class="paragraph">   
      Fue a partir de la Cámara de Davison que el Coronel Howard Vyse, haciendo uso de pólvora, descubrió los restantes techos y sus correspondientes cámaras intermedias. A cada una de ellas la designó con el nombre de algún personaje ilustre: Wellintong, Almirante Nelson, Lady Ann Arbuthnot y Coronel Campbell, respectivamente en orden ascendente. Vyse halló en las cámaras superiores unos jeroglíficos pintados en color rojo que los egiptólogos ingleses interpretaron como Khufu y año 17.
   </div>
   <div class="paragraph">   
      El conjunto de techos alcanza, a partir del piso de la cámara real, una altura de 21,107 m hasta el vértice inferior del gablete calizo, que se halla desplazado unos centímetros, probablemente debido a un asentamiento de los sillares del núcleo sobre los cuales transmite todo el peso de la mampostería superior. 
   </div>
   <div class="paragraph">   
      Los arquitectos franceses, Gilles Dormion y Jean-Patrice Goidin, creen que los asentamientos y las grietas en las vigas se deben a la presencia de la que sería la verdadera cámara funeraria de Quéope, todavía por descubrir, oculta sobre la Antecámara. *(Esta hipótesis es analizada en el capítulo 3: Las teorías funerarias) 
   </div>
   <div class="paragraph_m5">   
      Kerisel, por el contrario, atribuye el aplanamiento del gablete superior, el asentamiento de la pared sur y consecuentemente, las fisuras en los arquitrabes, a dos errores de construcción: 
   </div>
   <div class="paragraph_p40">   
      - Al empleo de un mortero a base de yeso impuro que, si recibe posteriores aportaciones de agua, desarrolla una enorme presión capaz de fracturar la piedra.
   </div>
   <div class="paragraph_p40_last">   
      - Al hecho de que los sillares que rodean y soportan las cámaras son de mejor calidad y forman una mampostería más compacta con juntas menores que la del resto del núcleo, el cual, dado el enorme peso de la sillería que soporta, se comprimió en mayor medida y en su asentamiento, arrastró la pared sur de la Cámara del Rey <font class="mark_text">[KERISEL, 1991, p.72]</font>.
   </div>
