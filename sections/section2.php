   <div class="subtitle">
      2.9 El Pasaje Descendente
   </div>
   <div class="paragraph">   
      Este largo y rectilíneo conducto <font class="mark_text">*(Fig.4)</font> mide 1,20 m de alto por 1,05 m de ancho. Con una pendiente de 26º 31' 23" según Petrie (Edgar, 26º 18' 10") y una longitud de 105,36 m, cruza en primer lugar toda la masa de sillería, a lo largo de 34 m, para continuar profundizando en la roca viva del subsuelo hasta desembocar en un corto pasillo horizontal, con un receso en la pared oeste, de menor sección y una longitud de 8,91 m, que conduce (previo escalón descendente) a la gran e inconclusa Cámara Subterránea.
