   <div class="subtitle">
      2.21 Sarcófago 
   </div>
   <div class="paragraph">   
      Tan compleja, costosa y magnifica estancia, solo contiene una especie de sarcófago de duro granito, vacío y roto en una de sus esquinas <font class="mark_text">*(Fig.17)</font>. No se han encontrado restos de su tapa, si es que algún día la tuvo. Está hecho por vaciado a partir de un solo bloque y es observable el desvío y la posterior corrección en la trayectoria de la sierra durante el proceso de corte. 
   </div>
   <div class="paragraph">   
      Hubo, forzosamente, de ser instalado en la cripta antes de proceder al cierre de la misma con las grandes vigas que conforman el techo; puesto que sus dimensiones externas: 2276, 978 y 1049 m/m, son mayores que las de la zona constreñida del Pasaje Ascendente <font class="mark_text">[PETRIE, 1990, p.30]</font>. 
   </div>
