   <div class="subtitle">
      2.22 Pozo y Gruta 
   </div>
   <div class="paragraph">   
      En el inicio de la Gran Galería, junto a la pared oeste, se halla la entrada a un pozo que desciende atravesando los sillares del núcleo de la pirámide para continuar perforando en la roca viva del subsuelo hasta comunicar con el extremo inferior del Pasaje Descendente. Está excavado de forma muy irregular y, a nivel de la roca incorporada a la base de la pirámide, comunica con una gruta (natural ?) o ensanchamiento que contiene un misterioso bloque de granito acunado al borde de otra cavidad algo más profunda <font class="mark_text">*(Fig.18)</font>.
   </div>
   <div class="paragraph">   
      El primer tramo del pozo, 7,96 m, es totalmente vertical y bien construido por medio de mampostería. A partir de aquí, se inclina hacia el sur excavado en los sillares del núcleo. La entrada a la gruta se halla reforzada con sillares bien aparejados. Prosigue descendiendo de forma irregular y excavado en la roca del subsuelo hasta alcanzar el extremo inferior del Pasaje Descendente, formando previamente un codo que rebasa en algo dicho pasaje. La longitud total es de unos 60 m <font class="mark_text">*(Fig.19)</font>.
   </div>
   <div class="paragraph">   
      El pozo había sido explorado por John Greaves, Nathaniel Davison y la expedición francesa de Bonaparte; no obstante fue el capitán Caviglia quien, en 1817, con tesón y en su obstinación consiguió desatascarlo y averiguar donde finalizaba; resolviendo así lo que durante siglos había constituido uno de los grandes misterios de la Gran Pirámide.
