   <div class="subtitle">
      2.3 Zona de construcción
   </div>
   <div class="paragraph">
      La zona elegida para edificar la pirámide presentaba un relieve rocoso que fue incorporado a la fábrica. Se desconoce la forma y el volumen de roca que se respetó. Se supone que fue nivelado dejando terrazas a distinta altura para prevenir posibles corrimientos de la obra en cualquier sentido. Esto dificulta el trazado del cuadrado de la base por la imposibilidad de medir las diagonales del mismo.
   </div>
   <div class="paragraph">
      Se conocen cinco puntos por donde puede constatarse la presencia de este promontorio rocoso: Primer tramo del corredor descendente, a+ 3 m. del nivel superior del zócalo considerado cono cota "0". En el pozo de servicio, donde la roca natural alcanza la cota +7 m. En el ángulo NE donde se aprecia la roca labrada en forma de sillar, hasta 1,95 m. para adaptarla al núcleo. También en los ángulos NO y SO. En este último alcanza el nivel de dos hiladas <font class="mark_text">[MARAGIOGLIO & RINALDI, 1965, IV, p.13]</font>.
   </div>
   <div class="paragraph">
      La roca que rodeaba este núcleo fue nivelada y sobre ella se colocó un pavimento perimetral de caliza blanca perfectamente plano y con juntas finísimas.
   </div>
   <div class="paragraph">
      Cole observó únicamente una diferencia de 21 milímetro entre la parte más baja que se halla en el centro de la cara norte y la más elevada, cerca del ángulo SE.
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      2.4 Zona perimetral externa
   </div>
   <div class="paragraph">
      Este zócalo sobresale de la pirámide 45 cm. al este, 42 cm. al norte, 35-38 al oeste y 30 cm. al sur. La altura de su contrahuella es de 55 cm. y presenta un ángulo respecto a la vertical de 2º-3º.
   </div>
   <div class="paragraph">
      En realidad no se trata de un pedestal o peana sino que el zócalo formaba parte del propio enlosado de la zona que bordeaba toda la pirámide a modo de témenos o períbolo, delimitado a su vez por un muro de piedra caliza de unos 9 m. de altura, y de 5 codos de grosor, hoy totalmente desaparecido. Se conservan restos de las entalladuras de cimentación de este muro perimetral a una distancia de 10,20 m. de la base de la pirámide al este y al norte y algo menos al sur y al oeste; de ello se puede deducir que el recinto tenía una anchura de 20 codos. Salvo en el lado este, hay indicios de un segundo muro a una distancia que oscila entre los 18,75 y los 23,6 m. del pie de la pirámide y de un grosor variable entre 2,5 y 3,6 m <br /><font class="mark_text">[GOYON, 1990, p. 247-248] [MARAGIOGLIO & RINALDI, 1965, IV, p.67]</font>. 
   </div>
   <div class="paragraph">
      Alrededor de la pirámide, a una distancia de 6 codos de la base y a intervalos de 3,5 m., fueron excavados pequeños pozos rectangulares, de 35 a 65 cm. de lado y de 40 a 60 cm. de profundidad. Se desconoce su función, aunque tenía que ser constructiva puesto que quedaban ocultos por el pavimento y, previamente a la colocación de este, se rellenaron con lascas y mortero. Es significativo que las cavidades de los extremos, coincidan exactamente con el límite del zócalo de cimentación y nivelación. 
   </div>
   <div class="paragraph">   
      En los ángulos NE, NO y SE, se excavaron depresiones rectangulares. Las septentrionales fueron descubiertas por la expedición francesa de Bonaparte en 1798 al retirar montones de escombros y arena que a lo largo de muchos años se habían ido acumulando.
   </div>
   <div class="paragraph">   
      Dichas entalladuras o "encastrements" permitieron determinar con exactitud la posición de las piedras angulares del desaparecido revestimiento y deducir, previo cálculo y en función del criterio de nivel adoptado, la medida original del cuadrado de la base.
   </div>
   <div class="paragraph">   
      La función de estas cavidades parece ser la de marcar los límites de la pirámide; no la de evitar posibles deslizamientos de la fachada puesto que la profundidad es muy desigual y en la esquina SO, donde falta la roca natural, se colocaron sillares de piedra caliza sin dotarla de dicha hipotética capacidad de fijación.
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      2.5 Constitución del núcleo
   </div>
   <div class="paragraph">   
      Formado por sillares de 1 m³ de media, dispuestos en hiladas horizontales. Las piedras, de caliza fósil, procedían en su mayoría del propio altiplano al sur de la pirámide, hasta las canteras más alejadas situadas al sureste de la pirámide de Micerino.
   </div>
   <div class="paragraph">   
      Petrie opinaba que se extrajeron de las canteras del lado arábigo del Nilo. Su densidad es de 2,354 Kg/dm³ <font class="mark_text">[SMYTH, 1978, p. 284] [KERISEL,1991, p.67]</font>. 
   </div>
   <div class="paragraph">   
      En la parte más interior de la fábrica, se alternan bloques de grandes dimensiones con otros menores. Las juntas están rellenas de un fino mortero de gran resistencia, de color rosado, formado por arena, yeso, cal y granito. Su densidad es de 2 Kg/dm³ <font class="mark_text">[KERISEL, 1991, p.67]</font>.  
   </div>
   <div class="paragraph">   
      Los sillares externos y los que rodean las cámaras están mejor labrados; son de dimensiones más regulares y presentan juntas más finas que los internos y los que ocupan niveles altos. La densidad media de la pirámide es de 2.000 Kg/m³; de ello se deduce que contiene de un 10 a un 15% de espacio vacío <font class="mark_text">[KERISEL, 1991, p.67]</font>.  
   </div>
   <div class="paragraph">   
      En general, la altura de las hiladas decrece a medida que se asciende; no obstante se dan numerosas excepciones sin que, en apariencia, la secuencia siga una norma o período que se repita. Según Petrie la mayoría de las hiladas tienen una altura comprendida entre 0,51 y 0,76 m. Las hiladas: 3-4-5-6-7-36-44 y 98, una altura de 1,02 m. Solamente las hiladas 1-2 y 35 superan 1,26 m. de altura.  
   </div>
   <div class="paragraph">   
      Goyon, cree que la diferencia de altura en las hiladas es consecuencia del variable grosor que, de forma natural, adoptan los estratos de piedra caliza en las canteras de la meseta, tal como puede constatarse en la Gran Esfinge.  
   </div>
   <div class="paragraph">   
      Las fachadas presentan una depresión a lo largo de la apotema. La mayor profundidad se da en la cara norte donde alcanza los 0,94 m. No hay unanimidad en cuanto a la finalidad de este ángulo diedro, ni si el posterior revestimiento también lo presentaba. Podría ser estética para resaltar las aristas y conseguir un efecto de <i>"recorte"</i> sobre el espacio; o bien técnica, a fin de evitar una posible deformación de las caras hacia el exterior ante un movimiento sísmico.  
   </div>
   <div class="paragraph">   
      Actualmente, el número de hiladas o niveles existentes es de 203; aunque las dos últimas solo disponen de escasos sillares.  
   </div>
   <div class="paragraph">   
      El número de hiladas supuestas oscila, según autores, entre 210 y 220.
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      2.6 Revestimiento
   </div>
   <div class="paragraph">   
      Estaba formado por hiladas horizontales de sillares de piedra caliza blanca, fina y compacta; cortados en forma de trapecio recto. Se conservan algunos bloques en buen estado en la cara norte y este. En las fachadas oeste y sur se hallan muy desgastados por la erosión del viento arenoso del desierto <font class="mark_text">*(Fig.1)</font>. 
   </div>
   <div class="paragraph">   
      La piedra, de mejor calidad que la del núcleo, procedía de las canteras de Tura, al otro lado del Nilo. Su densidad es de 2,5 Kg/dm³ <font class="mark_text">[KERISEL, 1991, p.67]</font>. 
   </div>  
   <div class="paragraph">   
      La altura de los bloques coincidiría con los respectivos sillares del núcleo; aunque es posible que algunos fueran equivalentes a dos hiladas superpuestas.
   </div>
   <div class="paragraph">   
      Las diferencias de nivel de la primera hilada de revestimiento entre fachadas, no llegan a 3 cm.
   </div>
   <div class="paragraph">   
      El espesor de las juntas entre bloques no sobrepasa el medio milímetro. Las uniones se rellenaron de una lechada muy líquida a base de yeso y extremadamente resistente. <i>"El yeso es empleado constantemente en la mampostería de las pirámides. Es una mezcla de cal ordinaria y de yeso de París, de carbonato y de sulfato de cal. Ignoramos totalmente como los obreros egipcios lo introducían en las juntas del revestimiento de las pirámides. Los bloques de la base pesan 16 toneladas, por consiguiente no es posible hacerlos resbalar para reducir el mortero; no obstante la junta vertical que tiene 1,5 m de altura y 2,15 m de anchura, está rellena de una capa de yeso de 1/20 parte de cm. de espesor. Las juntas de los pasajes y las cámaras están llenas de la misma materia pero, están tan apretadas, que apenas son perceptibles"</i> <font class="mark_text">[PETRIE, 1912, p.165]</font>. 
   </div>
   <div class="paragraph">   
      La pirámide fue despojada de su revestimiento probablemente a partir de la dominación árabe. La mezquita del sultán Hasan (1356), junto a la Ciudadela, está construida casi enteramente con sillares del revestimiento de la pirámide de Quéope.
   </div>
   <div class="paragraph">   
      El hecho de que solo hayan perdurado escasas piedras de la primera hilada del revestimiento y que el plano de asentamiento tenga una pequeña inclinación hacia el centro, hace muy difícil precisar su ángulo.
   </div>
   <div class="paragraph">   
      La media de diversas mediciones efectuadas por Petrie en los bloques de revestimiento de la cara norte y fragmentos de sillar, con diversos instrumentos, corresponde a un ángulo de 51º 50' 40" ± 1' 5" <font class="mark_text">[PETRIE, 1990, p.12]</font>. 
   </div>
   <div class="paragraph">   
      Esta magnitud es aceptada por Cole como media general para toda la pirámide. 
   </div>
   <div class="paragraph">   
      Según el arquitecto francés J.Ph. Lauer, el ángulo elegido sería: 51º 50' 35" que equivale a la relación 14/11= 1,2727 <font class="mark_text">[LAUER, 1988, p.232]</font>. 
   </div>
   <div class="paragraph">   
      Si la relación entre el perímetro de la base y la altura fuera <i>π</i> (Pi), el ángulo resultante sería: 51º 51' 14".
   </div>
   <div class="paragraph">   
      Si guardara la proporción <i>Φ</i> (Fi), o Razón Áurea, el ángulo sería: 51º 49' 38,2".
   </div>
   <div class="paragraph">   
      El cálculo de las restantes dimensiones se ha efectuado en base al ángulo teórico Fi; en todo caso las diferencias resultantes son mínimas.
   </div>
   <div class="paragraph_m5">   
      Altura hipotética de la pirámide: 146,51361 m.
   </div>
   <div class="paragraph_m5">   
      Longitud hipotética de la apotema: 186,36819 m.
   </div>
   <div class="paragraph">   
      Volumen hipotético de la pirámide: 2.591.701,6 m³.
   </div>
   <div class="paragraph">   
      Del testimonio de Diodoro podría inferirse que la pirámide carecía de ápice: 
   </div>
   <div class="paragraph">   
      <i>"La más grande de ellas, de forma cuadrangular, tiene por cada lado de la base, 7 pletros y más de 6 en altura. Se retrae, a partir de la base, de forma que en la cima cada lado no mide más de 6 codos"</i> <font class="mark_text">[DIODORO, 1865, LXIII, p.73]</font>.
   </div>
   <div class="paragraph">   
      Se trataría de una singularidad difícil de admitir puesto que otros autores clásicos, como Agatárquides de Cnido, hacen mención a la existencia del piramidión o última piedra en forma de pirámide.
   </div> 
   <div class="paragraph">   
      Por otra parte, se han encontrado los piramidiones, o parte de ellos, pertenecientes a otras pirámides. El Museo de El Cairo guarda varios ejemplares; algunos de ellos muy bien conservados. 
   </div>
   <div class="paragraph">   
      Recientemente se ha localizado en Guiza lo que parece ser, según Zahi Hawass, parte del desaparecido vértice de la pirámide de Quéope.
   </div>
