   <div class="subsubtitle">
      Closed pyramid
   </div>
   <div class="paragraph">
      Once the work has finished, the workmen took the counterweights off their sleighs and they left them on the gallery floors attached and ready for sliding.
   </div>
   <div class="paragraph">
      They dismantled all of the machinery of the system which was removed through the <i>"Ascending Passage"</i>.
   </div>
   <div class="paragraph">
      From the <i>"Grotto"</i>, the inferior channel of the <i>"Well-Shaft</i> was obstructed with the gravel and soil which, had been left there for this reason. This signified that the escape route of the workmen was not made by the <i>"Well-Shaft"</i> channel, but by the <i>"Ascending Passage"</i>.
   </div>
   <div class="paragraph">
      Once already out from the pyramid and by means of a very skill system of <i>"remote control"</i>, the three counterweights were released and consequently they immediately rushed down the gradient of the <i>"Ascending Passage"</i> until being retained and encased in the straitness of this conduct on its beginning for the purpose.
   </div>
   <div class="paragraph">
      The way leading to the chambers was thus blockaded till the men of Al-Mamún, in the IX century Ch.E., the tunnel in the ashlars close to the obstructing wedge was drilled and thus the <i>"Ascending Passage"</i> was discovered. 
   </div>
   <div class="paragraph">
      The automatic closing device which allowed the pyramid to be sealed from the outside without risk of being closed in and at the same time allowed the workmen to block up the well-shaft before leaving, was the following:
   </div>
   <div class="paragraph">
      Thick ropes were introduced through both air vents (the ones used on the rollers of the Antechamber would do) and met in the frontal part of the first stopper block. There, on the frontal part, the extremes of the ropes were tied by means of an <i>"Egyptian"</i> knot, which has the peculiarity of coming instantly undone, regardless of how tightly the extremes have been tensed, when the incorporated split pin was released and which at the same time was very easy to move.
   </div>
   <div class="paragraph">
      The confluence of the Grand Gallery and the Corridor of the Queen's Chamber was filled with salt, forming an embankment which united without presenting discontinuity, the floor of the gallery with the ascending duct. Then, they covered the whole floor of this passage with a fine coat of sand <font class="mark_text">*(Fig.12)</font>.
   </div>
   <div class="paragraph">
      With the same saline product, used in the zone of confluence, they blocked up the restricted end of the <i>"Ascending Passage"</i>.
   </div>
   <div class="paragraph">
      Immediately afterwards, they abandoned the pyramid here and through the <i>"Descending Passage"</i>. Everything was prepared to definitively close the accesses to the interior chambers.
   </div>
   <div class="paragraph">
      Through the <i>"Descending Passage"</i> the rope that was united to the split pin was pulled. It slipped between the knots and when it fell, the knot became undone.
   </div>
   <div class="paragraph">
      The blocks rapidly fell down the gallery slope; they crossed the salt bridge and entered into the <i>"Ascending Passage"</i>. The sand deposited made the slipping easier. The salt stopper compressed in such a way that it was capable of retaining the train of blocks. The only thing left to do was dissolve the salt stopper so that the obstructive wedge would slowly descend to find the compressed zone and remain jammed in it without breaking. 
   </div>
   <div class="paragraph">
      The water needed to enable the saline dissolution came from outside through the channels of the <i>"King's Chamber"</i> <font class="mark_text">*(Fig.13)</font>. 
   </div>
   <div class="paragraph">
      Stone fixed like a stopper in each of the exterior exits of the air vents and in the cavities, corresponding to the posts of sustention of rails and steps, was the last operation before removing the large ramp of stone and adobe which allowed the construction of the first sixty one courses.
   </div>
   <div class="paragraph">
      I calculate that about 400 workmen would be sufficient to attend all of the phases of the ascension system of the slabs.
   </div>
