   <div class="separator"></div>
   
   <div class="subsubtitle">
      The mechanism
   </div>
   <div class="paragraph">
      It is from the base of course 61, or the upper part of course 60, that the mechanism for elevation of the slabs by means of interior chambers comes into action. The disposition and functioning of the system which allowed the stones to be lifted was the following:
   </div>
   <div class="paragraph">
      The three great blocks of granite which form the obstructive wedge, at the beginning of the <i>"Ascending Passage"</i>, were situated on the higher extreme of the <i>"Great Gallery"</i>, immediately after the <i>"Great Step"</i> and mounted on wooden sleighs <font class="mark_text">*(Fig.3)</font>.
   </div>
   <div class="paragraph">
      When the anchors which retained the sleighs were released, they precipitated down the slope of the gallery acting as a counterweight (of the exterior slab) which actions a large transmission chain which, in turn, produced the rotation of a roller located inside the <i>"King's Chamber"</i> <font class="mark_text">*(Fig.4)</font>. 
   </div>
   <div class="paragraph">
      The roller of the royal chamber, interlocked with another one whose mission was to rewind a thick rope, of about 12 cm. diameter, which penetrated by the duct or vent of the South face and, after crossing the nucleus of masonry of the pyramid, reaching the exterior to unite with the device which elevated the slabs situated at the end of the ramp or zone of arrival of the stones.
   </div>
   <div class="paragraph">
      The <i>"Great Gallery"</i> in its entire length and in both corbels is provided with 27 cavities or slots distributed to leave regular spaces between them. The mission of these cavities was to anchor some wooden posts which reached in height up to the second salient of the wall, forming a structure capable of resisting the impact of the counterweight sleighs against the constraints which were distributed in a regular way along a handrail attached to the structure of the gallery.
   </div>
   <div class="paragraph">
      The route necessary to reach the vertex of the pyramid, from the exterior ramp, is exactly three time the length serviceable or available to move each sleigh in the interior of the gallery <font class="mark_text">*(Fig.5)</font>.
   </div>
   <div class="paragraph">
      This means that the sleighs were used separately and in a correlative way depending on the level where they were working. To position the first courses, as from the outside ramp, only the first counterweight was used; this kept stopping at a lower or more distant extreme as the courses or terraces were finished. Four courses above the hole of the air duct, the second counterweight also started to work as well as the first one; but whilst this one covered the whole length of the gallery without interruption, impacting against more and more distant extremes. The third counterweight only took part, along with the others, in the positioning of the final courses, end to end. 
   </div>
   <div class="paragraph">
      On the outside, the stones ascended mounted on runners and suspended by a crosspiece which slides over two wooden rails. These runners were fixed at a certain height parallel to facade of the pyramid and sustained by means of a wooden structure which was elongated by modules as the construction gained height. The driving motive kept moving to a higher level and, the space left by it was supplemented by a new section of rails. 
   </div>
   <div class="paragraph">
      The rails were supported by solid wooden posts, united to each other by means of cross reinforcements, and leaning on tiers formed by the absence of one of each three blocks of the own lining of the pyramid <font class="mark_text">*(Fig. 6)</font>.
   </div>
   <div class="paragraph">
      After unloading the stone on the pyramid, this had to be substituted by something which could pull the gallery counterweights until they reached the level of their starting point; meaning, next to the <i>"Great Step"</i>.
   </div>
   <div class="paragraph">
      The ascension of the counterweight sleighs was achieved by filling the area which was occupied by the stone with sand; although with a heavier weight. Two rows of workmen were situated on the gradient on either side of the rails, forming a double human chain, moving and ascending small sacks full of sand joined together by means of a rope. The sand used was always the same. It was unloaded onto the ascension platform, and when it reached the bottom it was rapidly lifted again, pulling the rope at the same time as other workmen, loaded and ascended another stone. In this manner when the slab was deposited on the terrace for its positioning, the sand, was ready to replace it. 
   </div>
   <div class="paragraph">
      Approximately 1.000.000 blocks or slabs were positioned in this way. I calculate that the pyramid would have in total, including the pyramidion, 217 courses of masonry which were constructed in the following way:
   </div>
   <div class="paragraph">
      The 61 course complete and one part of 62 to 71, following the ramp or inclined plane, located on the outside and on the South face. The other 146and the complementary part of the courses 62 to 71, by means of the mechanism of the interior chambers.
   </div>
   <div class="paragraph">
      The first counterweight would lift the slabs of 45 courses; in other words, it would complete the pyramid up to the higher level of course 106. The second counterweight, together with the first one, would lift the following 52 and would be cut off on the 158th. Finally, the third counterweight would intervene finishing off the pyramid with the final 59 courses, in their majority much smaller, with the exception of the last one or pyramidion. 
   </div>
   <div class="paragraph">
      The total volume of the pyramid was 2.591.701,60 cubic metres. The volume in the part destined to the mechanism of the chambers: 650.007,45 m³ <font class="mark_text">*(Fig.7)</font>. 
   </div>
   <div class="paragraph">
      The circuit covered by the elevator mechanism in the interior of the pyramid, occupied all of its chambers and galleries; except for he subterranean one <font class="mark_text">*(Fig.8)</font>. Its most remote extreme reached the confluence of the <i>"Ascending Passage"</i> with the <i>"Descending Passage"</i> area that is today occupied by the obstructive granite wedge. In this point there was a drum which inverted the direction of the chain coming from the gallery. Due to this, the passage to the chambers of the workmen who were in charge of the operation of the mechanism, was not made through the "Ascending Passage" but by means of the subterranean one and the passage of the <i>"Well-Shaft"</i>. This was the second and principal function of the irregular <i>"passage of the thief"</i>. 
   </div>
   <div class="paragraph">
      The area of confluence of the three passages (Grand Gallery, Ascending Passage and the Corridor of the Queen's Chamber) were provided with two tracks that united the gallery floor with the Ascending Passage; in such a way that the sleighs could run on them until reaching the North wall of the gallery. This design allowed the workmen to be situated in the first section of the flat corridor and have access to the lower part of the sleighs to make repairs or the corresponding maintenance to the runners.
   </div>
   <div class="paragraph">
      The chain proceeding from the Ascending Passage, before returning to the gears situated over the Great Step to close the circuit, penetrated the inside of the Queen's Chamber where a great wheel lathe could be found <font class="mark_text">*(Fig.9)</font>; a very necessary mechanism to enable an effective control of the movement of the counterweights.
   </div>
   <div class="paragraph">
      The chain was returned to the gallery by means of the gears which could be found in the confluence of the three passages or maintenance area.
   </div>
   <div class="paragraph">
      Due to the fact that the three counterweights had to intervene in a correlative way, it was necessary to have a mechanism for the coupling and uncoupling of the sleighs with the driving chain. The ropes which drove the connection clamps should always have just the right tension; this was the important mission of the <i>"Antechamber"</i> <font class="mark_text">*(Fig.10)</font>.
   </div>
   <div class="paragraph">
      By means of this construction system it is possible to deduce that, the exterior coating of the pyramid was put into position at the same time as the nucleus of masonry and one course ahead of this, with the exception of the space occupied by the ascension rails. This zone was finished from top to bottom (as the egyptian priests related to Herodotus) at the same time as they dismantled the whole framework of the exterior elevator system.
   </div>
   <div class="paragraph">
      Each course of coating had to be put into position on the pyramid before its equivalent in the nucleus. In any other way it would have been impossible to be able to adapt them with such precision. This allowed working <i>"from inside"</i> in the positioning and millimetric adjustment of the stones. This avoided, the need of having a surrounding ramp and that of situating an inclined plane on each of the four faces. The coating stones were hauled over the terrace formed by the slabs of the nucleus and put into position forming a perimeter ledge. Later, the interior space was filled with slabs, firmly joined to the coating stones, avoiding matching joints. Nevertheless, to put the last courses and the pyramidion into position, a lateral terrace had to be built allowing outside operation; due to the fact that the work space is progressively reduced as the summit is reached. It is due to this that the axis of the chambers and the south vent are displaced from the apothegm of the pyramid <font class="mark_text">*(Fig.11)</font>.
   </div>
