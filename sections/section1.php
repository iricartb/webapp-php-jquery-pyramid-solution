   <div class="subtitle">
      1.2 Máquinas
   </div>
   <div class="paragraph">
      Todos los ensayos de máquinas elevadoras inspiradas en el controvertido testimonio de Herodoto*: elevador en cigoñal o <i>Shaduf</i> de L. Croon y Crozat, cabrestantes abatibles de Strub Röessler, balancines de F. Petrie, G. Legrain y A. Choisy, básculas de J.P.Adam y J.Kerisel, palancas elevadoras de P. Hodges, o palancas de arrastre de L. Albertelli, resultan inoperantes o inverosímiles; en cualquier caso lentas y peligrosas.
   </div>
   <div class="paragraph">
      Exigen además que el revestimiento sea colocado en sentido descendente a partir del vértice, lo cual es inviable; o bien proceder a su corte y pulido una vez colocado, de lo cual resultaría un ingente e inasumible desperdicio de trabajo y material.
   </div>
   <div class="paragraph">
      En la Gran Pirámide, se hace todavía más evidente la imposibilidad del empleo de maquinas semejantes, dada la existencia de los grandes bloques de granito de 50 a 70 toneladas que conforman los cinco techos planos de la Cámara del Rey. 
   </div>
   <div class="paragraph_alert">
      La conclusión es que no es posible edificar las dos grandes pirámides de Guiza valiéndose de las máquinas propuestas; ni siquiera proseguir con ellas la labor inicial de una rampa de construcción. 
   </div>

   <div class="paragraph">
      * <i>"La pirámide fue edificándose de modo que en ella quedasen unas gradas o apoyos, que algunos llaman escalas y otros altares. Hecha así desde el principio la parte inferior, iban levantándose y subiendo las piedras, ya labradas, con cierta máquina formada de maderos cortos, que, alzándolas desde el suelo, las ponía en el primer orden de gradas, desde el cual, con otra máquina que en él tenían prevenida, la subían al segundo orden, donde las cargaban sobre otra máquina semejante, prosiguiendo así en subirla, pues parece que, cuantos eran los ordenes de gradas, tantas eran en número las máquinas, o quizá no siendo más que una fácilmente transportable, la irían mudando de grada en grada, cada vez que la descargasen de la piedra; que bueno es dar de todo diversas explicaciones. Así es que la fachada empezó a pulirse desde arriba, bajando después consecutivamente, de modo que la parte inferior, que estribaba en el mismo suelo, fue la postrera en recibir la última mano"</i> <font class="mark_text">[HERODOTO, 1968, CXXV p.169]</font>.
   </div>
