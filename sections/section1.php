<div id="section">
   <div class="title">
      1. Estado de la cuestión
   </div>
   <div class="paragraph">
      La construcción de las grandes pirámides de Egipto es un tema no resuelto de forma satisfactoria y como confiesa Georges Goyon, constituye uno de los problemas más irritantes de la arqueología egipcia <font class="mark_text">[GOYON, 1990, p.15]</font>. 
   </div>
   <div class="paragraph">
      Jean kerisel hace un diagnóstico muy acertado cuando dice que todos los intentos para explicar la construcción de la Gran Pirámide, pueden clasificarse en dos grupos o familias mutuamente excluyentes: los defensores del plano inclinado o rampa (1-1), y los partidarios de las máquinas (1-2), siendo más probable que la solución radique en una combinación de ambos sistemas <font class="mark_text">[KERISEL, 1991, p.81]</font>. 
   </div>
   
   <div class="separator"></div>
   
   <div class="subtitle">
      1.1 Rampas
   </div>
   <div class="paragraph">
      La rampa es el único método constructivo del que existen evidencias arqueológicas; no obstante, pretender que el plano inclinado alcance el vértice de la Gran Pirámide, situado a más de 146 m. de la base, plantea insuperables problemas de ingeniería y arqueológicos; ya sea por la excesiva longitud de la calzada resultante, el inmenso volumen del terraplén que se genera (rampas de Petrie, Borchardt); o por el excesivo desnivel del plano cuando se pretende soslayar dichos inconvenientes (rampa de J.Ph.Lauer).
   </div>
   <div class="paragraph">
      Si se opta por el modelo de rampas zigzagueantes (Hölscher), o envolventes (N.F.Wheeler, G. Goyon), surge la falta de estabilidad en los terraplenes y la imposibilidad de maniobrar en las esquinas.
   </div>
   <div class="paragraph">
      Las rampas mixtas de Dieter Arnold y Mark Lehner, evitan el problema del distanciamiento, pero agravan el de la pendiente; al tiempo que reúnen, sin resolver, los inconvenientes propios de cada uno de los diferentes planos combinados.
   </div>
   <div class="paragraph">
      Los conocimientos arqueológicos actuales de la meseta de Guiza, reducen sensiblemente las posibilidades en cuanto al lugar de emplazamiento del plano inclinado en la Gran Pirámide; puesto que:
   </div>
   <div class="paragraph">
      Al norte el terreno desciende bruscamente y el terraplén impediría la entrada a las cámaras interiores de la pirámide.
   </div>
   <div class="paragraph">
      Tanto el lado este como el oeste constituyen una necrópolis, con numerosas mastabas alineadas según un orden preestablecido y, a su vez, contemporáneas a la construcción de la pirámide.
   </div>
   <div class="paragraph">
      Según Zahi Hawass, en el lado oriental se ubican tumbas correspondientes al año 12 del reinado de Khufu (Quéope) y en el lado occidental las tumbas de funcionarios se levantaron durante el quinto año de reinado.
   </div>
   <div class="paragraph">
      Por contra, las tumbas del lado sur corresponden al reinado de Micerino y las fosas de los barcos son de la época de Dyedefre; es decir, posteriores a Quéope.
   </div>
   <div class="paragraph">   
      Hay que considerar también que, la mayor parte de la piedra caliza fósil utilizada para construir el núcleo de mampostería de las pirámides, procedía de las canteras situadas al sur de Quéope y al sureste de Micerino; de acuerdo con el estudio topográfico y arqueológico de la meseta llevado a cabo por Mark Lehner: <i>A contextual approach to the Giza pyramids</i>. 
   </div>
   
   <div class="paragraph">   
      Atendiendo pues a los condicionantes técnicos y arqueológicos podemos establecer que el plano inclinado para construir la Gran Pirámide debería ajustarse a los siguientes parámetros:
   </div>
   <div class="paragraph_p40">   
      - Emplazarse en la fachada sur de la pirámide.
   </div>
   <div class="paragraph_p40">   
      - Prolongarse longitudinalmente en esta misma dirección, sin presentar variaciones de pendiente en la calzada.
   </div>
   <div class="paragraph_p40">   
      - No sobrepasar las más lejanas canteras de la propia meseta, localizadas al sur de Waddi; unos 1000 m. como máximo desde el centro de la pirámide. 
   </div>
   <div class="paragraph_p40">   
      - El punto más alejado de esta rampa debería situarse en una zona comprendida entre los llamados Talleres de Quefrén y la Excavación de Kromer, que parece ser el vertedero de las rampas de construcción y de los restos culturales del campamento de los obreros.  
   </div>
   <div class="paragraph_p40_last">   
      - La pendiente de la rampa no debería en ningún caso superar el 8% admitido por Jean Kerisel y, a ser posible, adoptar el valor recomendado por Georges Goyón, 5,6%.   
   </div>
   
   <div class="paragraph">   
      No obstante, una rampa que se ajustara a estos parámetros alcanzaría, como máximo, 76 m. de altura en Quéope y 36 m. en Quefrén. 
   </div>
   <div class="paragraph_alert">   
      Por consiguiente, se puede afirmar que no es posible edificar las dos grandes pirámides de Guiza valiéndose únicamente del recurso constructivo del plano inclinado o rampa.
   </div>
